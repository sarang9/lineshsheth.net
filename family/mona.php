<p>Dear Mona,</p>


<p>Rare are instances that a father-in-law writes a meaningful letter to a daughter-in-law.  That requires a very different kind of relationship that helps communication beyond social barriers.</p>

<p>Anish was so shy before he met you that I never thought he can even fall in love with a girl. And he is steadfast in relationship he develops. That is a rare quality in him. But after meeting you, you both passed through lot of turmoil before you both reached the present state which is robust and continually developing. </p>

<p>I have seen your life as metamorphosis of personal development. You transformed from a traditional, shy, reserved girl into an outgoing, bold and creative person through you own initiative in which Anish’s contribution was immensely meaningful. I think that the change of stage for you came when you shifted from residence at Archana to your new residence at  Cosmos B in kandivali. And another big change – which in fact was a ‘leap’ – came who you left you personal Interior Decoration practice to your creative work with the firm of N. R. Jasani. You broke many barriers and you opened up into a landscape that was far larger than domesticity to which you were bound. The biggest beneficiary of your shift from Archana was Manav who came across a new world of opportunities for development. The whole thing looks, in perspective, so exciting and meaningful. </p>

<p>Being a mother, a housewife and a working woman, all at the same time, is a task that calls for great adaptability. I admire your sense of balance in maintaining this triad of life.  </p>

<p>Your sense of designing is simply creative, not complex. It has a built-in practicality of approach. Design you did for Archana, years back, was so neat that we have not changed even a small thing since you made it. It still looks fresh and inviting. That is your hallmark in all the designs you have done. </p>

<p>Behaviourally you are simple, forthright and adjustable. It is a big quality. </p>

<p>Mona, Manav is your future and Anish’s hope. He is your  strength of the house and star of our family. A lot depends on how he develops. In the letter I have written to Manav, there are hints of a possible direction for life. Read it carefully. Preserve the letter, laminate if for next generation. He may find it meaningful after he grows into an adult. Take care of the book "An autobiography of seeker" by Siddharth its a book of teaching, practice and experience of meditation. </p>

<p>Bharatiben looks towards you and Anish with lots of assurance. Keep that trust intact. Sometimes it is interesting to find ways to remember the needs of other- for example let her choose dinner for all three of you or go to the film she wants to see. Allot time for her needs. Mona, in general you keep good health but do not take it for granted. Inculcate in Manav the culture to take care of health and mental clarity. Or else he will follow your culture as you follow your family culture, not his own independant culture. </p>

<p>What we ignore and do not acknowledge dies gradually. It applies to our body, mind and our relationship. Think over deeply.  </p>

<p>I have not much more to tell you. I do not know when current of life in this body that keeps it alive will choose to depart. I would never regret its disconnection. I will welcome it for, I identify myself not with my body, but with that undying liveliness. It has already lasted past 70 and I feel grateful to it. </p>

<p>Mona, I do not know if there will be another time available for me to ask for heartfelt apology for any hurt may have caused you and your family by my words or by my actions. I want to leave this world without any illfeeling towards anyone. </p>

<p>Receive lots of affection and lots of respect.</p>




