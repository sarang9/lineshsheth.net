<p>Learning from the experience i passed through in my way of doing business, he was firm in creating money on his own rather than running the business in traditinal way. He was right on the dot in his understanding. With his ideas, he not only changed the shape of Total Tools size and scale, he also changed the pattern of doing the business in the group. </p><!-- <div class="col-lg-2 pull-right">
