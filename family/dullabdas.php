                    <h3 align="center">Dullabhadas</h3>
                    <br>
                    <div class="col-lg-12" style="text-align:justify; font-size:16px;">





<div class="col-lg-4" style="margin:0px 10px; padding:0px;">
<img class="img-responsive" src="../images/20161208_172810.jpg">
</div>

<p>Dullabhadas Fulchand, my father living in Latur during 1900's, was the eldest son of Fulchand Chapsi. He was fair skinned, very handsome, tall, well built and always smiling.He was educated possibly upto 8th or 9th standard in Gujarati. He had beautiful handwriting. He had working knowledge of english. He was very generous, creative, hard working and a complete family loving man. He was the main hope and strength of my grandfather. His main interest was in healing people free of cost through home made medicines. So he chose to start business of selling medicines while he himself used to make several Ointment, Balms and Tinctures at home.</p>
<div class="col-lg-4 pull-right" style="margin:0px 10px; padding:0px;">
<img class="img-responsive" src="../images/veena3-music-new1.jpg"><br>
<img class="img-responsive" src="../images/ads_image_1261466679128.jpg">
</div>


<p>His other interest was Indian classical music, Marathi natya sangeet and bhajans. Therefore along with medicines, he would also sell Harmoniums, Tabla, Flutes, Gramophones of His Masters Voice(HMV), and Records. Because of his kind and philanthropic nature, people in the whole town used to like him. He was the Man of the Town.</p>

<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img class="img-responsive" src="../images/img1467875417405.png">
<label style="color:#000;">Harsuklal & Mohini</label>

<img class="img-responsive" src="../images/IMG-20161203-WA0003.jpg">
<label style="color:#000;">Priyakant & Vasanti</label>

</div>



<p>He was married to my mother at Bombay in very interesting situation. This is described in mother's story.</p>
<p> My parents had four sons and two daughters. Vasanti, who married to Priyakant at Bombay was eldest. Then i was next. Bodh Kumar was third. Mohini, married to Harsukhlal(Harshesh and Naxita's father) was fourth, Lokendra was fifth and Jitesh was the youngest(see photograph below and on sides).  </p>

<p>One particular habit that endeared him to his children was his habit of listening to children and always dining together with them in one plate. His distinct feature was his roaring laughter and a very playful nature. </p>
<div class="col-lg-6 pull-right" style="margin:0px 10px; padding:0px;">
<img class="img-responsive" src="../images/20161209_173828.jpg">
<label style="color:#000;">from left - Lokendra, Kumar, Jitesh, Linesh</label>

</div>

<div class="col-lg-4" style="margin:0px 10px; padding:0px;">
<img class="img-responsive" src="../images/20161209_171951.jpg">
</div>
<p style="text-align:justify;">I was influenced by him very deeply and i still hold him as an ideal image. He had great trust in me. He hoped that i would one day shift the part of the family from small town of Latur to a city like Mumbai. I followed his ideals, his hopes and images. I was able to do what he had wished, when i established my home in mumbai.</p>

<p>My father died during his angiography test at jaslok hospital in mumbai at the age of about 64.</p>



