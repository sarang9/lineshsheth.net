                    <h3 align="center">Fulchand Chapsi</h3>
                    <br>
                    <div class="col-lg-12" style="text-align:justify; font-size:16px;">


<div class="col-lg-4" style="margin:0px 10px; padding:0px;">
<img src="../images/IMG-20160706-WA0056.jpg" class="img-responsive lsshadow">
<img src="../images/IMG-20160706-WA0055.jpg" class="img-responsive lsshadow">
</div>


<p>My grandfather Fulchand was a fair skinned, thin and tall man. He used to wear a Gandhi cap and dhoti. He was hardly educated. He first came to Latur. In those days around 1910, India was under British rule and Latur was under the Kingdom of Nizam The Mughal King Osman Ali of Hydrabad. Latur fell under Hydrabad region.</p>
<div class="col-lg-4 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/sir-osman-ali-khan.jpg" class="img-responsive lsshadow">
</div> 
<p>I do not know from where My grandfather came. He had some farms but he was never a farmer. To my memory he lent money to local people and earned his livehood from the interest he received. He was married to Mrs. Rukhmani, My Grandmother, who was a very simple, uneducated, kind and home loving devoted woman.</p>





<p>Mr. Fulchand were four brothers including himself. He had three sons and a daughter my father Dullabhdas was eldest. My father chose to do business in selling of medicines. So our family was established in medical business. General condition of the family was of a medium class struggling for money and survival. </p>
<div class="col-lg-4 pull-right">

<img src="../images/fatther-mother.jpg" class="img-responsive lsshadow">
<label style="color:#000;">Dullabhadas & Prabhavati Sheth</label>
<img src="../images/20161209_162008.jpg" class="img-responsive lsshadow">
<label style="color:#000;">Chandulal & Pramilaben Sheth</label>
<img src="../images/20161209_162104.jpg" class="img-responsive lsshadow">
<label style="color:#000;">Chandanmani & Hukumchand Sheth</label>

</div>



<p>I have a memory that my grandfather had interest in magic, music and playing cards. This interest also reflected in my fathers business. His store was called Imperial Medical Musical and General Goods Store.</p> 
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/The-Magic-Blog.jpg" class="img-responsive lsshadow">
</div> 
<p> My grandfather used to call roadside magicians at home and would ask them to perform for the neighbourhood children. After the show, he would finally buy all the tricks and know-how from the magician. He used to teach some of those tricks to me as a child. He was a simple, ethical man.</p>
<p><b style="color:#000; font-style:italic;"> His three sons were named Dulabhadas, Chandulal and Hukumchand and a daughter called Hiraben.</b></p>

<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/2116873476.jpg" class="img-responsive lsshadow">
</div> 
<p>Latur is a small town located in Maharastra near Karnataka border. In those days, around 1920's the town had no electricity, no connectivity with other towns, water was scarce, there was no industry. Only occupation was farming and dealing in daily necessities. There were just two or three schools with basic infrastructure. The town seemed to have no future because of its isolation. But My grandfather was content with whatever he could earn with his means. So Latur became our home town. we had no connection with Gujarat.</p>

<p> We spoke Gujarati at home but our native languages were Marathi and Hindi. We were all educated in Hindi.</p>



