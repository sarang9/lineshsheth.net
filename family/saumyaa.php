<p>Dear Saumyaa,</p>

<p>I am your Dada. My name is Linesh. Now I am past 70 and you are just 2½ years.</p>

<p>I write this so that one day you will be able to read this letter coming from someone who loved you every inch, who liked your smile, your brilliance, your love for your Pappa and your Mom.</p>

<p>I do not know whether I will be there when you are able to read this but that does not matter. What matter is that you came into our life as a bundle of joy and energy, intelligence personified. I want you to know that we all liked you intensely. We liked your presence in our lives.</p>

<p>Saumyaa, my dear, you will never know how much your dad-my son Amit, and your Mom Nehal loved you, cared for you, worked for you  to grow you with joy and dignity. I say this because, your parents will never tell you what I am telling you and you will require someone else to tell this to you.</p>

<p>Saumyaa, as you grow, you will exist into a world quite different in values of life, beliefs and  traditions in which I and your grand-mother – Lina lived. Values, beliefs and traditions change with time. What does not change in human relationship is the value we have for love, togetherness and caring for each other.</p>

<p>Always learn to rise beyond your personal self since one's personal world is always small, petty and selfish. When you care for others, you expand your world, you expand your heart and head so that you make space for others to live together in harmony with you. </p>

<p>That is what I learnt in my life and that is exactly what I wish I am able to do when I am born again. So whenever or wherever you see someone living for welfare of others genuinely, lives and teaches unselfishness, be sure ‘I am there’. You will remember me.</p>

<p>Saumyaa, grow in life with values that give dignity to human beings at all levels. Grow to live above differences, grow to live in a way that tells you to forgive others, to help those in difficulty and to live with immense joy. Never forget to smile when others hurt you and to laugh at your own faults.</p>
 
<p>Preserve and read this letter time and again as ‘thoughts repeated with faith produce and attract actions of maximum strength’.</p>

<div style="border:1px solid red;"></div>

<p><b>Note:-</b></p>
<p>Saumyaa:</p>

<p>My Grandfather never told me where he came from and conditions in which he lived. He also did not tell me what were his aspirations in life nor, what he was good at, nor what was special about him that I could learn. I did not know what he expected from his next generation, what were his thoughts, beliefs and traditions. I wish he had seriously told me all this so that I could have known where I came from, tradition I belonged to and how I connect with my roots.</p>

<p>Saumyaa, I wish to compensate in your life what I missed in my life. I want you to know who were your ancestors, times in which they lived, the way they thought about life etc. That is the way to connect with your personal history and your historical roots.</p>

<p>Saumyaa, I came from Latur, a place about 500 k.m. from Mumbai, in Maharastra. I was born and brought up there with a Maharashtrian cultural background. I passed matriculation from Latur. I was first among 33000 students in district, educated in Hindi Medium. Then I moved to Hyderabad, now in Andhra Pradesh. I lived in hostels with a cosmopolitan environment and passed B.E, Bachelor of Mechanical Engineering.</p>

<p>I came to Mumbai in search of livelihood. With very modest means, i survived, by serving initially for four years. Then i started a business with whatever was my strengths. That was 1965. See my historical background and the colleges i work with in this website under family and relationship. </p>

<p>Around 1985 I started factories in Latur where we produced knives for wood, paper and metal. They were machine knives, not hand knives. Companies we started were called Wudtools and Industrial Products Co. Lokendra, my brother, was my partner. I have two other brothers Jitesh and Kumar who live in Latur, and two sisters Vasanti, Nailesh's mother, and Mohini, Kosha's gandmother and Harshesh's mother. In Latur, we have our ancestral home. However, from your father's side, we originally come from Mangrol, near Junagadh, in Gujarat.</p>

<p>I started Total Tools and Equipments Private Limited in the year around 1996. Your dad, Amit joined me in business around 2001. Prior to this, Anish had already joined me. It was the pair of Anish and Amit that brought turnaround in our economic situation. Rest, your father will tell.</p>

<p>I came to Mumbai to earn money but soon realized that I need to know things about our inner world, the world of my which struggles to be happy in life and yet experiences sufferings. It is a world one wants to learn how to overcome suffering, as a compelling priority of life Saumyaa i learnt this through my work with Silo of Humanist Movement. </p>

<p>That search led me to spirituality, the science of that describes multidimensional nature of man and universe. I learnt from different masters about essentials of what builds joy, happiness and energetic world within us. Since all these resided inside ourselves, I learnt to start looking in words deeply. The process was called meditation in which I became both a teacher and an unfailing practioner- starting with 20 minutes, reaching 2 hours daily. I also wrote two books- one on business & other on my experiences in meditation, written by entitled "An Autobiography by a seeker", written by my student and colleges, Siddharth Gehlot who lives in MA, USA, you will find his page in this website. I will leave the book with your parent for you to and learn essentials of life.</p>

<p>Your Grandmother Lina was the most beautiful, kind and loving lady, I married. She took intense care of me, you, your dad and mom. In fact she connected with everyone with love and care and was central to our family.</p>




