<p>Dear Avinash,</p>

<p>I never knew who my Nana was. He lived in Mangrol, Gujarat, before my mother married my father. I did not know even his name nor my Nani’s. I did not know who they were, what they were and how were their lives. </p>

<p>From that background I deeply value the fact that I, as  your Nana, know you so well that I have never felt that you are my second generation. We are more friends than anything else. We think, talk and behave without feelings of a generation gap. </p>

<p>Avinash, credit for this closeness goes to you and Abhishek as you never communicated with me as if I was a man of another generation. You communicate with equal level, yet with lots of respect for each other. </p>

<p>Avinash, both you and Abhishek are smart as you are also handsome! You are more of a thinking man, cordial and go-getter. Avinash if I have to value you, I would not do it on basis of what you are, I would do it on basis of what you can be. Your value lies in your potential, your will to unfold who you are ! </p>

<p>Work on oneself starts with the way one starts his mornings. The value one gives to the mornings, the fresh start of the day, decides how his day would be. </p>

<p>If we start our day with yesterday's body and yesterday’s mind, that will decide how eventually we will behave with our career and with people. Then circumstances, moods and our physical conditions will decide everything. </p>

<p>So learn to treat your mornings with great respect. Allot time for developmental reading. Do your yoga, work on breath and learn to stay quiet for at least 15 minutes every morning. It is a lifelong appointment with life. The most important decision that will decide this will be your resolve to get up at least half an hour to one hour earlier than the way you do today. Be firm on your getting up time, irrespective of your sleeping time. This is your priority of life.</p>

<p> More you know about this aspect through different teachers and books, more your understanding of the ‘meaning’ of life will increase. Without this basic understanding, life is lived like a robot, engaged in mechanicalness   of daily routine. Then daily routine governs the life, 'we' do not govern life. </p>

<p>Emotionally, your biggest strength is Abhishek. You have tremendous love for him. You care, you understand and you can do anything for him. To have such a companionship in life is very precious, very unitive for growth and for cementing family bonds. You have to now decide what role you will play for the family & for business. That will guide your way to the future of who you will be. </p>

<p><b>For example, I asked once my father what he expects of me. He told me, “we have no home in Bombay, you set up a home and call everyone”. That became my mission of life. That expression shaped my career. </b></p>

<p>When I met Nani before marriage, I told her my mission and she said <b>"I will adjust my life in the way it is necessary to you to live"</b>. My biography of career and life are known to you and you also know my relationship with all my brothers & cousins. That is how the role you choose to play shapes your life. It is an important choice. If you are responsible, you will take people together, shine as a coordinator, and stand as a rock in front of difficulties. It is good that you build your image of what you want to be. </p>

<p>Avinash, know that ‘Jack of all and master of none’ is what most people are made of. Value lies, recognition lies, in mastering two thing – one, what is necessary in life and two, what is voluntary, what you do of your own volition. Only one who chooses to live according his volition, will add joy and youth to his life. All the rest will live a stressful life of "compulsions" that has no "time" as its chief characteristic. </p>

<p>Mastering what is necessary for life, like being a good husband, a good administrator or a boss, or mastering some aspects of trade, bring efficiency in career. Mastering what is voluntary covers aspects that are not necessary for life but are essential for our growth, for unfolding of our inner potential. </p>

<p>In general, voluntary aspect also include developing traits that are close to your heart. Like, I chose to master meditation, breathing techniques, languages, voluntary work such as those i did with Humanist Movement, my involvement with Vivekanand Hospital and my teaching the esoteric & spiritual paths to people. </p>

<p>You have to choose your own voluntary aspects. Mastering them always gives substance to life, makes you distinct from what is ordinary & routine, and produces immense joy. When you determine to be a master, two things happen – one, you cease to be an amateur and two, you begin to teach others, you become a reference to follow. Think deeply on this. </p>

<p>I wish you read my book – "my experiences with meditation and meditative techniques". It is attched in this website as my book of experiences containing 10 chapters. Follow what appeals to you & learn from teachers. Join a path & walk on it. </p>

<p>Avin, you are very dear to me. I like you so much. I have tremendous respect for who you are today. I will have infinitely more respect for what you will be in life. I wish you have same faith & respect for yourself. </p>





