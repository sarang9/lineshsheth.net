<p>The engagement between my father and mother was sort of an accident. My father used to have redness in the eyes due to some heat in the body. The family whose daughter was to be engaged with him, thought he was drinking. With this fear, they broke the engagement on the very day on which ceremony was to be performed. They did not appear on the occasion.</p>

<p>This was supposed to be inauspicious. But a neighbour in the family who knew my mother's family proposed her for engagement. In that moment, everything was then agreed in the circumtances and my father got engaged with my mother without knowing who she was and how she looked like.</p>
