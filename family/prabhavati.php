<p> Throughout her life she was very proud of me, had great love and had very high expectation from me because of my brilliant and studious background of education. She chose Lina, my wife, very painstakingly with lot of effort and care. I immensly feel grateful to her for this very important contribution to my life. She always depended on me, with certainity, that i would be the one who will establish for the family, the first home in Mumbai. All circumtances favoured me to achieve this goal somehow. I always felt that her blessings played a vital role in this accomplishment.</p>


<p>She had a very tragic childhood background. She told me that her family had a dispute with a man in the town. The man was a lofer. He used to be in love with my mother's sister.</p>

<p>When he was denied a marrige proposal by her father, he crashed one day in the middle of the night in their house. He killed her parents and one of her brothers in front of her eyes. This was a big truma in her life which showed its scars throughout her life.</p>
<p>My mother was very courageous, very protective, practical and a complete woman of the family. She had great contribution in the life of his sons and daughters and we all greatly felt indebted.</p>
