<p>At the same time i was also intimate with masters like Mahesh Yogi, Silo, Andrew Cohen, Fernando etc. Many of them visited my home and spent meaningful time. However i did not meet Viktor Frankl in person but was highly motivated by his teaching on meaning of life. I studied a distant learning diploma course on Introduction to Logothrepy known as "Franklian Psychology" offered by Viktor Frankl institute of Logotherepy in USA. It provided me with a new insight on looking at everything in life from a meaningful point of view.</p>

<p>The conducting thread of my life was always to learn its new dimensions, to implement them by engagement and finally to move towards remaining free from responsibilites of running them with liabilities. So i started new companies, new products, new collabrations on one hand and handing over their charge to the next generation on the other hand. Finally the approch was to remain relatively free from liabilities. As a result, there came a time in around 2010  parted from Wudtools and also from major part of my share from Total Tools and Equipment Private Limited. This move was liberating. It gave me inspiration to plan a meaningful evening of life to spend free time with myself, my family and my spiritual persuits.</p>

<p>In a nutshell, this is how i arrived at a point in life, where friendship with my ownself, my body, my mind and my life force began. In this journey of life i had the fortune and opportunity to be associated, engaged and work actively with lots of my colleagues from Humanist Movement, business envirnoment, spiritual path, music and literature. Since these are all very important, i have devoted a special module in this website for these colleagues. Please refer to this module seperately. This journey has been interestingly fascinating, inwardly eventful and outwardly harmonious. Today, at 78, life looks beautiful and contented. </p>
