<p>I was born on 11th july 1939 in Latur. In those days, we lived in a joint family in our ancestral home called Rukmini Nivas. We lived together with my grandfather and uncles with their families. I roughly remember that at one point of time we were about 40 people living under one roof with one kitchen.</p>

<p>Our family was called Chapsi family, known after father of my grandfather. Our family was well known but not very prosperous. We were having a business of medicines, cycles and musical instruments. There was a separate shop for medicines handled by my uncles. I grew fulfilling just bare necessities of life. Since Latur had no connectivity with other towns nor with any big cities, i had hardly any expanded vision of the world. Everything was local and narrow. </p>

<p>Main population among which we lived were Maharastrians, Marvaris, Lingayats, Muslims and Gujaratis. I was fairly good in studies and passed my matriculation in hindi medium from Marvadi Rajasthan School. For graduation in engineering, my parents sent me to Hydrabad, Andra Pradesh. I passed B.E(Mech) from Osmania Engineering College in 1961.</p>

<p>Soon i was employed in Hydrabad Asbestos Limited as a shift engineer. Many times i worked for all 24 hrs. in the factory. The job was routine. There was not much to learn. Then i lived in Marvadi Rajasthan hostel with all marvadi friends. Many of them are close to me even today.</p>
<div class="col-lg-4" style="margin:0px 10px; padding:0px;">
<img src="../images/20161208_172810.jpg" class="img-responsive">
</div>
<p> My father always gave me an image of my being an employer, not an employee. He said "you cannot be employed", because you are a son of a businessman. This was a very strong image coming from a person i loved most in my life. That image drew me one day from Hyderabad to Bombay without any particular agenda. </p>

<p>I came to Bombay in 1962 mainly with the idea of starting a business to fulfill the image that my father gave me. I stayed in Narsinh Hindu Lodge at Rs.5/- per day. And then as a paying guest in Mangrole Nivas for Rs.60/- per month.</p>
<div class="col-lg-5 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/20161208_181930.jpg" class="img-responsive">
</div>
<p>In 1963 i got married to Lina was living at that time at Mint Road near V.T. station. Somehow i found a job in Philips India Limited at Mazgaon in Mumbai at Rs.400/- per month. The job was repetitive. So i shifted as a sales engineer to Batliboi and Company at Naval Dockyard near museum in Bombay. I was put in charge of woodworking machines, imported from Czechoslovakia. Within a small time, i gained an expertise in woodworking machines, particularly Woodworking Tools.</p>

<p>Based on this experience i started my own company called Wudtools with a table space, given to me free of charge as a friendly gesture, in Dalal Street. This table space belonged to Mr. Chunnilal shah of Greentose Pvt Ltd, who were selling rubber belts. Eventually Mr. Chunnilal developed a great trust in me. As a result, his son Satish Gothi, a mechanical engineer was married to my cousin sister Vidya.</p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img  src="../images/DSC_0152.JPG" class="img-responsive">
</div>
<p>Over a small period of time Wudtools' business grew and Lokendra, my third younger brother, joined me as a partner. Wudtools shifted to more spacious space at Dada Manzil on mohamad Ali Road. Basically Wudtools was a trading company sourcing tools from Mr.Jaswant singh Bhachoo of Bhachoo Industries at Goregaon in Bombay and, also from some of our competitors.</p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img  src="../images/we-name.jpg" class="img-responsive">
</div>
<p>Over a period of time trading in knives and saws became more difficult. So it became necessary to open a manufacturing unit. Latur was just coming up as new MIDC town and sheds were available at low cost. We bought several sheds and started manufacture of peeling knives for wood veneering, Carbide Tipped Saw and Circular Knives for slitting paper.</p>

<p>Gradually Lokendra, my brother, who was also a Mechanical Engineer, put considerable interest in running Wudtools. In a short time he was able to handle both the factories as well as the sales network with his own ideas. That left me free for development of other products. </p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img  src="../images/logo_eng.jpg" class="img-responsive">
</div>
<p> However the main thing it did was that it gave me free time to pursue my interest in Humanist Movement. I had inclination to take deep interest in getting more insights into spiritual aspects of life. It was thanks to this freedom, made available by Lokendra's overall skill in handling business, that i was able to spend time with the masters listed here in a separate module. It also gave me time to practice their methods.</p>

<p>It was in 1990's that Manmohan Singh announced India as a Free market, as compared to license oriented closed economy. The world was becoming open to India. I intuitively sensed that time has come for us to manufacture new generation tools in India or to work in association with the world leaders, hand-in-hand, to market their tools to meet the future needs. </p>

<p> Not knowing which tools to trade in, i chose a general name called Total Tools for the new trading company. It then occurred to me that over a period of time, we may also have to deal with equipment’s which employ these tools. Therefore the final name of the company established around 1995 was called Total Tools and Equipment Private Limited. </p>

<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img  src="../images/total-logo1.png" class="img-responsive">
</div>
<p>Total Tools started from Mohamad Ali Road, shifted for some years to our premises in the name of Industrial Product Company, at 328 TV Industrial Estate at Worli. From there it was shifted back to Mohamad Ali Road and then to Pravasi Industrial Estate, Goregaon and now at, A/57 Giriraj Industrial Estate on Mahakali Caves Road at Andheri East, where about 30 people could sit on 1+2 floors.</p>


<p>Multiplicity of interests in life was never felt by me as a division of my attention. I was devoted, multi-focused and engaged actively with business interests, spirituality, classical music, philosophy, meditations, writing and reading. </p>

<p>I believed that, as a general rule, i was undividedly engaged with what i was interested in at a particular time. Multiple interests did not look like diversions. I saw them as a bouquet that made the life aesthetic, diverse and interesting. </p>
