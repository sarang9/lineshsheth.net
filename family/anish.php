<p>Anish, now 46, was born in 1970 in Mumbai. Health was not his strong point during childhood.In his growing period as a child, i was in the midst of building my career. It required me to travel continuously throughout India and sometimes out of India. That hardly gave me much time to look after him with special attention, as compared to today's parents.</p>

<p>The credit for Anish's growth goes almost fully to my wife Lina.She was extremly careful and adamant in pursuing his education. Anish passed matriculation from Rose Manor Garden School Santacruz, not with an outstanding record. However i had very high hopes on him. By reciprocation, he had lot of faith in my vision of things about his future. </p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img src="../images/20161222_182407.jpg" class="img-responsive">
</div> 
<p>It was during school days at Rose Manor that he had intimate realationship with Mona. Their relationship was genuine and they were committed to marry. As they were, they looked a very fine pair, compatible and completely in love with each other. Their togetherness was refreshing. </p>

<p>I wanted him not to pursue a graduation in engineering, but specifically to do a diploma in Mechanical Engineering. I wanted him to do specialization in toolings, which was central to our business. He exactly followed that. He passed diploma in engineering from Vartak Polytechnic in Vasai and did specialzed tooling course from Bhagubai Polytechnic, Vile Parle.</p>

<p>As he joined the family business, i could gradually discover that he was creative, with strength in searching and imaging. His perspective about overall grasp of things and analysis of situations in life or business was unusual. As a result, he developed the business of toolings with a deeper technical perspective since he had good knowledge about applications of toolings and diagnosis of faults.</p>
<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
