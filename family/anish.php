<p>Basically Anish is a hardcore family man, deeply in love with his family, including family of Mona, his beloved wife. Among the family circles, even after nearly eighteen years of marriage, people see both Anish and Mona as Romeo and Juliet. </p>

<p>Hobbies of Anish are photography- he did a spcialized course in photography- and singing. He likes to perform on stage as an amateur singer, as deep familarity with film songs of different genre. Along with his large circle of friends he has even started a group of amateur singer to perform regularly on stage. Anish likes fun, humor and company of friends.Lately, he has become a yoga enthusiast and it appears that health is now his special interest. Somehow Anish has gathered a special interest in the field of medicine, understanding of medical intricacies and communication with doctors from point of view medical relevance.</p>
