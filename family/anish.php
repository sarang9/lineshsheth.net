<p>Dear Anish,</p>

<p>My father was very kind. He was very ideal.  I respected him for everything he was. I admired him, loved him, followed him and to this day, I have not allowed him to die.  He "lives" in my heart as I always smile while remembering him.  Yet I regret that it never occurred to me to ask him to leave the essence of life, as he knew, in writing for me to contemplate upon.  He had beautiful handwriting, though.</p>
<p>I never knew who my Nana was nor did I know my grandfather intimately as he always treated me like a child, not the way I treat Manav today.  So I thought I should break this tradition of incommunication between generations.</p>
<p>Each one of us should know his roots, the landscape in which his forefathers lived, who they were and what were their values of life.  If possible generations should communicate among themselves with lots of love, if they happen to live & exist during same period of time.  We have right to know our history, or to know geography, from which we derive our origins.</p>
<p>So, I thought I should write to the members of generations I live together with and tell them whatever I could, that lends meaning to our collective life.</p>
<p>Anish, I feel, we all are living in times wherein there is crisis of recognition of others’ contribution in our lives. There exist a crisis of appreciation of what positives others are made of.  We have become so ‘busy’ – so, self-centred, that we have almost forgotten this invaluable necessity of life.  At best what we are left with are ‘cards’ that mechanically thank & appreciate everyone in a telegraphic language. The cards miss the depth of expression.  From that point of view, I find you as very sensitive in both recognition and appreciation. I think few have known me as intimately as you have.  I feel grateful to you for being so close to me.</p>
<p>It took quite some time for you to ‘arrive’ in my life!  You were beset with your own personal difficulties, mainly concerning your individual family. I do not know what happened but suddenly i saw you ‘arriving’ in my life, to take charge, to guide and to conduct the business operations.</p>
<p>I always felt very grateful to you that you listened to me in following your educational career so that you could be useful to the trade we are in. And you developed deep insights into application of tools we were selling.  Although you were reserved always in your expressions, I saw you developing good writing skills. That skill gave you ability to put substance and meaning into your communications with customers as well as with family members.</p>
<p>No one appreciated or read the ‘The Book’, i wrote, except you. You made maximum use of the book in our business – you put value to it.  Through the trait of contemplation – a faculty of looking deeply into things – you developed insights into what our business is, where we can go and how. This was your most valuable contribution.</p>
<p>When Amit came, you found a buddy that fulfilled what you lacked. You appreciated it, liked it and took a peculiarly complementing role.  That made Total, what it is today.</p>
<p>Your understanding of Mona’s life situation was unique. It was beyond what a doctor can understand. You carved out an exit from a possible abyss.  I admire your faculty of insight.</p>
<p>I have always found you free from resentment (stored anger), jealousy, competitiveness or revenge.  You reconcile fast with anyone, you repair what is damaged in relationships and you create new opening when things are blocked.  That is what makes you an invaluable asset.</p>
<p>Yet, lethargy – both bodily and mental – is your main enemy.  You need to be pushed/prodded into taking action.  You understand your body’s conditions and harm it can do to you. However your mind does not support your body to move to exercise, to jog, to choose to do Yoga. You do not know Anish but simplest way out of this is to ask for help.  Find a friend, join a class for a long time (not short time), or get a very regular teacher that wakes you up very early to set a new, refreshing routine.  I had same problem and I asked for a solution to Fernando.  It was Fernando who showed me the way and I hired Shastriji for 2 years.</p>
<p>Anish you also have good inslight for new places and their judgements.  I see that you replicate me in a new way in this aspect. We all owe a lot to your initiative in adding this dimension.</p>
<p>There is lot to mention about your proximity and creativity regarding relationship with our collaborators. With addition of Matrix you have started chartering new dimensions. Your complemention with Amit, your support to him and your understanding are all foundation that makes us who we are.</p>
<p>I know how much you care and contribute to Manav’s life.  It is more than what I can say.</p>
<p>Mummy finds peace when he thinks of you and I find support and understanding in you.  All this I will carry with me as essence, as your love, when I depart.</p> 
<p>I think life may survive a few more years but usefulness of body-mind organisation can cease earlier. It is in those times, when one is no longer able to do what he wishes, that one needs help.  And I and mom count upon all of you.</p>
<p>It was all joy living with you Anish.  I think you have more contribution in my life than I have in yours.  Yet I ask for your forgiveness, your generosity for hurt me and mom may have ever caused you or your family in any way.</p>





