<label>&nbsp;&nbsp;&nbsp;Toolings Private Limited</label>
</div>
<p>As an extension of Total Tools, he was instrumental, along with Amit, in starting a new venture, called MTS Toolings Private Limited. MTS initially collaborated with Matrix toolings of Italy, Specializing in Turret Punching and Bending Tools. The factory was located in Pune. MTS toolings, at this moment, has a good potential that needs to be unfolded.</p>

