<p>Years back when i was in high school, my history teacher asked me to name the Mughal Empire successors in india. As a part of my study of Mughal history, i was able to name seven successors from Babar down to Bahadur Shah Zafar II. Then my teacher asked me to name seven successors of my family. I was dumb-founded as a child!. I did not know anything beyond my grandfather, who was just my second generation. When the teacher further asked me what i knew about the lives of my father and grandfather, i was further surprised that i hardly knew about their lives.</p>

<p>Today i understand that we are living with scant regards for our family history. We hardly know about the particularities, the characters and the conditions in which our immediate ancestors lived. I realize that we owe large part of who we are to the generations that preceded us as our fathers, grandfathers, great grandfathers and so on. Yet we hardly know about them.</p>

