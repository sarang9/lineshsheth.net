                      <li>A simple practice for boosting your concentration to help you reduce inner distraction and enable you to bring an “easeful focused attention” to your meditation.</li>
                      <li>How to leave your “inner critic” on the temple steps so that you can allow your meditation to unfold naturally without the distraction of worrying whether you’re “doing it right.”</li>
