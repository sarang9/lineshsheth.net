<p>At the time when i was participating very actively in Humanist Movement, Nailesh was more often observer of how me, Lokendra and others were involved. Nailesh happened to be my sister Vasanti and Priyakant Dalal's son.</p>
<div class="col-lg-3" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/facebook_1484217777657.jpg" class="img-responsive">
</div>
<p>Nailesh says, he saw the way we networked with strangers and known people in Humanist Movemnt to work together joyfully, actively and resolutely. He says, he was seeing similar situations of networking in the joint families he lived with. There they shared the relationships, avoid isolation and some, like his father Priyakant and mother Vasanti, chose to take responsibilities for maintaining, sustaining and continuing the harmony in midst of diversities and differences. </p>
<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/logo_eng.jpg" class="img-responsive">
</div>
<p>Vasanti, his mother, belonged to Latur where Nailesh saw another joint family with more or less same structural composition and relationships. In Latur he had another experience of joint family networking.</p>

<p>He was greatly influenced by the culture set up by his parents, more particulary by his father Priyakant, in honest and transperent living, in hard work and in sincerity. Money was not a particular problem. </p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img src="../images/both.jpg" class="img-responsive">
</div>
<p>However, when he began to live for sometime independent of his father, after his marriage, he was looking for a change. He saw models of better internal composition in Jayesh Shah , Parimal Merchant and me. That was inspiration for him to start to work with Jayesh, who guided him with lot of materials , directions and commitement.</p>

<p>Inspiration, enthusiaum and overflowing energy made Nailesh start several groups, sometimes groups in the morining and also in the evening with neighbours relatives amd friends. A particularity of Nailesh was that he had very friendly and homely relationships with his customers. As a result, he had his first three monthly sessional meeting with attendance of about 150 people. Jayesh was surprised at this gathering of people. Nailesh's motivation was doulbled. Then on a new stage started - he began to take responsibillity for the important tasks that Humanist Movement needed organizationally, financialy and administrately. </p>

<p>Influenced by the spread of the magazine, Humanscape , managed by Jayesh Shah, he was inspired to work sometimes 10 groups. He also started travelling to distant places like Gujrat, Orissa, Tamin Nadu, Rajasthan, Delhi for spreading the message. Eventually in Jharkhand he went, on invitation, alone as a stranger, and was instrumental in influencing and starting large groups. He found everywhere, to his surprise, that people understood him, Humanist Movement and its proposals easily as if they were their own aspirations. </p>

<p>Today Nailesh is basically the link and the bridge between the Humanist members all over India. Like his father, he is an expert organizer, generous in his approach and very resourceful. As far as his efforts in Humanist Movement are concerned, Nailesh is a workhorse, an achiever and essentially a connecting link at all levels. He is basically a passionate unifier of people. </p>

<p>Nailesh says he was influenced in his learnings from Jayesh, Parimal, Fernando, his wife Smruti and other family members. He says he learnt from me deeper aspects of Humanist subjects as i had grasped the essense of spirituality that various faiths propagated from my closeness with masters and the teachings of their faiths. He liked this aspects of having insights of the different dimensions of humanist doctrine.</p>

<p>Working with Nailesh for years together, as a team, has been very enriching, helpful and interactive. I feel nice always that it was my close relationship with his father Priyakant that somehow Nailesh found his way into the Humanist Movement. </p>


