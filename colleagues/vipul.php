                      <div style="text-align:justify; font-size:16px;">
 

<p>I met Vipul in 1985. Around that period he was working as an engineer with Tata Consulting Engineers, Mumbai. I saw Vipul as a man of energy, receptive to new ideas.He was a man with an urge to understand whether there is "something" to life that gives human being a reason to live beyond survival. </p>

<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img  src="../images/ForLineshbhai.jpg" class="img-responsive">
</div>

<p>Vipul is a householder married to Rekha. He has one daughter who has today become a doctor in physiotherapy and a son studying in 9th standard. Rekha herself worked for sometime for the Humanist Movement. For her daughter Isha, Vipul is a life's model who has put in tremendous contribution to what she is today. I attach this letter here as it is one of the ideal letters in which a daughter has expressed candid emotions and gratitude to her father.</p>


<p>To begin with, he joined Humanist Movement out of curiosity in 1986. However when he found selfless, well intentioned and earnestly active people in the humanist meetings, he felt that this was the kind of envirnoment he was looking for. </p>
<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
  <img src="../images/20170116_145022.jpg" class="img-responsive">
</div>
<p>The initial introduction soon gave way to study of our internal world. The study of internal world, as explained in Silo's book, "The internal Landscape", describes that direction of life essentially consists of living with Internal Unity. One's Internal Unity gets disrupted when one lives a life of contradiction and conflict. Material progress in life is external. What really matters is internal progress. And internal progress is a process of moving from internal conflicts and contradictions towards unity with one ownself and others. Vipul says, that clarification he received during 1985 to 1987, when he was working with me, led to the discovery that meaning of life gets unfolded when there appears a purpose for living.<br><br> </p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
  <img src="../images/logo_eng.jpg" class="img-responsive">
</div>
<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
  <img src="../images/Humanist_party.jpg" class="img-responsive">
</div>
<p>The ways to resolve the contradictions in life got unfolded for him when he attended the Center of Work in 1987. That clarification, together with constant work for the Humanist Party, the political wing of Humanist movement, increased his faith in understanding that working for survival is ofcourse a neccessity for life, but purpose of living lies in discovering what gives meaning to it. Vipul says, both the purpose and the meaning started unfolding when he began to experience what it means to work selflessly, without violence and in interest of helping others to discover their meaning of life. </p>


<p>Vipul emphasized during our discussion that when he stared his own group, stared printing monthly newsletters, preparing it by getting up at 5:30 in the morning and uniting people together for a cause beyond self interest, his self confidence tremendously increased. That confidence produced strength of conviction in him. That conviction produced energy and, above all, a spirit that fulfils his existence when he treats others in the way he himself wants to be treated. </p>
<div class="col-lg-4" style="margin:0px 10px; padding:0px;">
  <img src="../images/scaffolding.jpg" class="img-responsive">
</div>
<p>Vipul particularly refered to the mention made by Silo in "The Internal Landscape" that activities of life are scaffholding for building our internal unity. He began to understand the practical aspect of transcendence in life when Silo said, "Love the reality that you build and not even death will detain your flight!". </p>
<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
  <img src="../images/leptir.jpg" class="img-responsive">
</div>
<p>He loved the image of human being by Silo in following words:
"Namer of a thousand names, maker of meanings, transformer of the world...your parents and the parents of your parents continue in you. Yor are not a fallen meteor, but a brilliant comet that flies towards the skies. You are the meaning of the world and when you clarify your meaning you illuminate the earth. When you lose your meaning, the earth becomes darkened and the abyss opens."  (The Internal Landscape)</p>


<p>It has been wonderful to be associated with Vipul for over 30 years. I have seen him when he entered Humanist Movement as a curious visitor. Today i see him as a committed man with conviction about direction of his life and his role in the world that surrounds him.</p>



