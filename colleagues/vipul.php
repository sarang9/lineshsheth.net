                                  <h2 style="font-family: 'Oswald', sans-serif;"><center><br>A meaningful letter from a daughter to her father<br></center></h2>

<div class="col-lg-4" style="margin:10px 10px; padding:0px;">
<img  src="../images/PA170164.JPG" class="img-responsive">
<label>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Isha Mody</label>
</div>

<p>Inspiration. Motivation. Achiever. A lot of people have a very formal bond with their fathers; they have mixed feelings of respect, love & fear. In a typical Indian household, there are boundaries, between a father & daughter, boundaries which restrict the relation they share.
My story differs a little there. </p>

<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img  src="../images/pro.jpg" class="img-responsive">
<label>&nbsp;&nbsp;Ascend and Add</label>
</div>
<p>My father has brought me up with minimal boundaries, allowing me to explore everything that I want to, allowing me to be everything that I want to, teaching me to be fearless, to not be held back. Growing up, my father would talk to me about living, ways of living, life, people, the mind & its psychology, science, history, English, everything. Long conversations with a little child who may have understood only half of what he spoke, who looked upon him like he was the world, like he had the answer to any question I could possibly ask, like he was the solution to any problem there could be, like he could protect me from any harm that was out there, I saw him as my Super dad.</p>

<p>As I entered my teens, & started to explore everything around me, I realized that maybe my father doesn’t have the answer to everything, he isn’t invincible, he isn’t the human encyclopedia that I thought he was. </p>

<p>He is a normal human being, just like everybody else, maybe with the same problems that are faced by everybody else. What a disappointment, huh? The person I thought of as a hero was just a regular person.
Reality-struck-me failed to see the real hero in him.</p>

<p>The hero who built his life with his own bare hands, the hero who took me to every book fare & bought me as many books as I pleased, the hero who had once proudly carried me on his shoulders when I refused to walk, the hero who has worked hard to give me the life I have, the hero who never forgot through all of this, that he had to continue to be my encyclopedia, whether it was science or life or philosophy.</p>


<div class="col-lg-4 pull-right" style="margin:10px 20px; padding:0px;">
<img  src="../images/Army-medals.jpg" class="img-responsive">
</div>
<p>The hero who had guided me & allowed me to study whatever it is that I like, the hero who has found the time to motivate me when I needed motivation, to push me when I needed a push & let me be when I needed to be independent. The hero who has not once under-estimated me, when the world doubted my abilities, he made me see what I was & am capable of & inspired me to go seize all the opportunities that came my way. 
</p>

<p>He has taught me to be realistic, to be practical & see life as an opportunity to serve mankind. He has been there for me when I needed inspiration & his words have always inspired me to work harder than I think I can. Also, he has been there for me, understanding when I have failed.
</p>

<p>He has been the friend I needed, the teacher who guided me, the idol who inspired me, letting me know that I can always talk to him freely & he’d never be too busy to hear me out.</p>
<p>He’s my hero.</p>
<p>Isha Mody (Year 2014)</p>

