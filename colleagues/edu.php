                  <div class="col-lg-9" style="background-color:#fff; color:#000;">
                    <h3 align="center">About Eduardo</h3>
                    <div align="center">
                      <img style="height:250px;" class="img-responsive lsshadow" src="../images/edu.jpg">
                    </div>
                    <br>
                      <div style="text-align:justify; font-size:16px;">
                        


<div class="col-lg-4 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/edu1.jpg" class="img-responsive">
</div>
<p>Eduardo Santos met me in the year 2004 in India. The fact is that Eduardo came later into my personal life but he first appeared as a milestone in the life of our group of companies. He came as our connective link to the resources in the world so that we could collaborate, construct and live on with the collaborators as business partners. What struck me about Eduardo was his transparent and friendly cordiality, flow of communication and openness to understand what i wanted.</p> 
<p>There hangs quite a story of our togetherness to the extent that we became personal friends. I wanted to know more about what made Eduardo what he is, and what was behind his universality of character. So i took his permission to dwell into his life history, as a story of networking of relationships in life.</p>
<div class="col-lg-2 pull-right" style="margin:0px; padding:0px;">
<img src="../images/tl.png" class="img-responsive">
</div>
<p>Eduardo told me that at the age of 5, his grandmother used to tell things to him with a clear-cut honesty that what she told were lies. Eduardo says, right at that time, he developed irreverence and disrespect for lies, as a way of communication. So, from the age of 5, it inherently went into his consciousness that a good way of life is one that is not based on lies. This was an important conviction at a very early age.</p>

<p>As he grew further in his life, as a contrasting background of his childhood revelation, it went on becoming clear that a good way of life is based on being honest, transparent and basically truthful. These three aspects formed a direction, guideline and a way of life. </p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/foot.jpg" class="img-responsive">
</div>
<p>Eduardo graduated as a Mechanical Engineer from Uruguay, the country of his origin. Teaching mathematics in Uruguay became his early professional career. However his main interest, up to age of 23, was football. Football remained as an early source of income but Eduardo says, living only to earn money was not central to his life, since he was well provided by his family. His guiding principle in life was to do what he "liked most".</p>
<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/starktools.png" class="img-responsive">
</div>
<p>Shift to Italy, from Uruguay, brought him in contact with Stark as the Area Manager. Since its founder Antonio trusted him as his guide for connectivity with the world, he became his confidant. Upon passing away of Antonio, he thought it fit to leave the next generation of Stark owners free to work their own way. But he was concerned with the interests of the customers he had developed  in India, Argentina, Middle East, North Africa, Spain, Portugal, Turkey etc., while being in Stark as an area manager.</p>
<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/fo.png" class="img-responsive">
</div>
<p>Upon leaving Stark, a new world of opportunities opened before Eduardo. The restrictions imposed by a singular product line, a single company and a country gave way to openness to the new products, peoples, cultures and host of new countries. The visits to trade shows and exhibitions held around the world opened the doors for new connectivity among diverse countries, cultures, peoples and products. When i asked Eduardo what is the new mindset that is appropriate for dealing with such diverse landscape, he said that international business is challenging but the main clue lies in allowing space and acceptance of differences in ways of doing business for each country. The point of view is to find congruence, adaptation and growing togetherness. He says his comprehension of the world view has paved ways to collaborations among partners of very different sorts.</p>


<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/total-logo.png" class="img-responsive">
<label>&nbsp;&nbsp;&nbsp;Tooling's Private Limited</label>
</div>
<p>Eduardo left Stark around May 2008 after 12 years but his relationship with the owners of Stark has continued very cordially until this day. However, the freedom of vision it opened brought him closer to Matrix in Italy, the makers of Turret punching tools. Eduardo was mainly instrumental in bringing Matrix to India to have a joint collaboration with Total Tools to create manufacturing facility in Pune. That facility is today known as MTS.</p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/euro.jpg" class="img-responsive">
</div>
<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/euj.jpg" class="img-responsive">
</div>
<p>It was during same period that he had two brief but very cordial meetings with Eugenio of Euromac, Italy, the makers of Turret Punching Presses. It is an evidence of the trustworthiness of Eduardo that Eugenio decided to open up Euromac India with Total Tools just after two meetings between them in Italy. Today, 2017, Euromac India is on its way to establish itself with selling and servicing. </p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/syn.jpeg" class="img-responsive">
</div>
<p>Eduardo thinks that he sees a great future in bringing China to work together with India and South America. he says, China has great infrastructure, speed and freedom to do what one likes. As an evolution, Eduardo's vision is changing from selling of products to creation of companies, and formulation of business models. In his view, bringing about Synergy by way of co-operation between two or more organizations and their facilities produce a combined effect that is greater than sum of their separate effects.</p>
<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/ct.jpg" class="img-responsive">
</div>
<p>According to Eduardo, this way of working has a distinct advantage. It creates conditions for coming together and basically creates richness for all who are involved. Eduardo says that the conducting thread for such way of working is to be transparent, without prejudice, with no strong likes and dislikes. This is because the world is made up of multiplicity, variety and apparent differences of cultures and languages. However the spirit of coming together transcends all of them.</p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/ind.jpg" class="img-responsive">
</div>
<p>Eudardo realizes that digitization has been essential part of the third industrial revolution but the world is moving towards the fourth industrial revolution which consists of robotics, Internet of things, nano technology and an importance of distributions, logistics and concept of flow. His future would soon be centered around the concepts of the fourth industrial revolution which is just beginning.</p>
<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/rich.jpg" class="img-responsive">
</div>
<p><br>Working for years with Eduardo has been a revealing experience for me. I did not find him to be a hard core negotiator in business where money and profits are in center. I found him to be a bridge between peoples and cultures. I have never found that he imposed any idea or a deal on anyone. He left, all who were involved, free to take their own decisions. I discovered that what always worked was his "presence". All who are involved confide in him, what is crucial to them. Eduardo is a "rich" man.</p> 

<p>He feels fulfilled in life even without possessions.</p>










