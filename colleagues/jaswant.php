<p>When i met Jaswant Singh Bhachoo in 1964, i was at the concluding phase of my employment of Batliboi and Company, a machine tool provider from different countries of origin. In those days, i wanted to make some spare tools for machines we were selling for Woodworking. Mr. Bhachoo then ran a small scale industry in Goregaon, making woodworking tools mainly for railways.</p>

<p>What i remember in my first meeting was his great self-confidence, irrespective of suitable industrial equipment in his factory. He was basically a man of skill, improvisation and inbuilt capacity to complete things, even in face of difficulties.</p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img src="../images/we-name.jpg" class="img-responsive">
