<p>I started the company <b>Wudtools</b> around 1965. He was then my main technical support, but beyond that he was my trustworthy and affectionate friend. Irrespective of our educational and age difference, we shared mutual trust, interdependent and clarity of togetherness. </p>
<div class="col-lg-2" style="margin:0px 10px; padding:0px;">
<img src="../images/dreamer.jpg" class="img-responsive">
</div>
<p>Mr. Bhachoo was a dreamer; he wished that someday he settles his future in USA. He also dreamed of makings films someday, for which he told me he had an interesting story ready with him so when i asked him what was a story, he said it was "Love Letters". He always wished to be short of any insufficiencies of life.</p>

<p>All said and done, he was basically a simple, honest and family loving man. His greatest support came from his wife Santosh who has been, even today, in my image, a model of courage, patience and resilience.</p>

<p>Bhachoo was not very educated man but in me, as a friend and as a business colleague, he found a source of completing administrative and financial formalities so that he could set up an industry of his vision. Along with his industrial vision, he developed a passion for acquiring properties for the purpose. He could then somehow manage to buy real estates that, his son Tony tells me, is also the basis of what the family is today in term of financial wealth.</p>
