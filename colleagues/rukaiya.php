<p>Overall, Rukaiya's entry into my life, with my family and in Humanist Movement has been constructive, engaging and meaningful. Because of her profession as a teacher, she has a facility for dialogue, constructive enquiry and in grasping useful understanding. It is this trait of hers that is the bridge of communication between us on many crucial aspects of life. </p>







               


