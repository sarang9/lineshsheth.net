<p><b>“Everything can be taken from a man but one thing: the last of the human freedoms — to choose one’s attitude in any given set of circumstances, to choose one’s own way.”</b></p>
<p><b>By Viktor Frankl</b></p>

<p>Rukaiya possibly did not know what Viktor Frankl had said years ago, but she somehow examplified the above statement. Rukaiya is a muslim, married to Kirit Joshi, a Bhramin, out of sheer commitment they made to each other. In face of difficult circumtances of life, they both chose the human freedom and attitude to follow their own way. </p>

<p>Both were highly educated. this courage to marry in face of resistance from the tradition indicates their strength, character and conviction about the value they attached to their meaning of life.</p>
