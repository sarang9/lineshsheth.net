<p>I met Rukaiya through a common friend, when Kirit was diagnosed with cancer in the year 1998. Kirit believed that he can be cured with Trascendental Meditation. During that period i was already a teacher of TM, taught to me by Maharishi Mahesh Yogi. Kirit passed away in process but Rukaiya chose to learn further  meditation from me. At one point of time. I introduced Humanist Movement to her. She was fascinated by a statement in Silo's book, called The Inner Look which said "There is no meaning in life, if everything ends with death".</p>
<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
<img src="../images/SPJainLogo.jpg" class="img-responsive">
</div>
<p>It was a fortunate coincidence that out of the synchrony of meeting between me, Dr. Manu Kothari and Dr. Shrikant that Rukaiya joined SP Jain school of management at Andheri.</p>

<p>As the shock of losing a spouse got reconciled, living circumtances gave way to openness of life. As Rukaiya began to meet diverse sections of people, she got more inspired to participate actively in Humanist Meetings and retreats. Her conviction to make life more meaningful led her to work in Nehru Nagar slum, to give support and direction to people living in difficult circumstances.</p>

<p>Although i did not have much opportunity to orient Rukaiya in internal work of Humanist Movement, she was greatly inflenced by my activities in diverse fields. She liked my  participation as a trustee in Vivekanand Hospital at Latur, in earthquake relief there, and in collection of sizeable funds. </p>

<p>It was interesting for her to see me learning about man's internal world from different masters in the world, synthesizing their teachings into different practices and instantly giving them to others voluntarily. She learnt the importace of unfailingness in spiritual practices and putting them into our daily life as a conscious behaviour.</p>

<p>Rukaiya believes in Humanism, in Gandhism and she appreaciates Vinobha's thoughts. In general, she directs her life with a voluntary spirit towards others. She develops long and meaningful relationships with people. She lives in great harmony with her family and with her surrounding even when she has, many a time, a dissenting stand in certain circumstances.  </p>
