<p>Latur was struck by a massive earthquake in 1993. Dr. Bharadia was the first person to get into action very early in the morning. He did a marathon work in moving the wheels of things that mattered, from rescue point of view. He was the first one to discover an almost new born child, in garbages of earthquake. He picked up the child, a girl, and took care of her until she was adopted by responsible and dignified parents.</p>

<p>During the earthquake, me and Dr. Bharadia travelled throughout India to collect funds. I put up a large advertisement in 25 newspapers. I took care of donors in Bomaby, including the donations offered by various Jain Temples in Bombay. Later me and Dr. Bharadia travelled throughout India for the cause of alleviating the suffering that earthquake had caused. Together, donations worth Rs. 5 crore were collected then. </p>

