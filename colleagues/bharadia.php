<p>Our friendship grew into creating mutual environment in which Dr. Bharadia began to learn various systems of brain breathing, energization of the whole body and personal wellbeing from me. Until today, Dr. Bharadia says, he follows these practices during his early morning’s yoga personal practice sessions. </p>

<p>Dr. Bharadia had highly concerned, participative and active intervention whenever serious illness occurred in my family at Bombay, whether it was my Angioplasty, Bypass surgery, Mona's difficulties, my wife's illness, Priyakant and Vasanti's illnesscess etc. That participation was not just consulting, it was very constructive. Psychologically it fullfiled the role of alleviating the pain and suffering in the family. </p>

<p>Dr. Bharadia has two sons, Rahul and Nitin , Rahul is a chief executive in a large corporate house. Nitin does his own business. Besides he has a daughter, called Munshi, very happily married in Narasimhapur.</p>

