<p>Around 1979 my orientor in Humanist Movement suggested me that i enlarge my internal world of action. He asked, me as a first step, to adopt my larger family, including my uncles, cousins and their sons as my own real family. That meant i had to take responsibility of their welfare and development. As a result, i had, in my small home at Santacruz, my brothers and cousins residing together as one family. I took responsibility of their marriages, education, and their development. My wife Lina undertook the relationships and marriages of over 10 to 14 brothers and cousins from my little home. </p>

<p>Later my orientor said that i should further enlarge my world by adopting my native town Latur. When i asked how i can do this, he said, relationship with people of Latur are less important for you than your concern for taking care to acquire means of overcoming their pain and suffering. It was that idea that brought me in touch with Dr. Bharadia in Latur in 1980's. He was one of the founders of <a href="http://vivekanandhospital.in/adminitrationn" target="_blank">Vivekanand Hospital</a>, Latur along with Dr. Ashok Kukade and Dr. Alurkar. I visualized that the best i can do overcome pain and suffering of people in the town of Latur was to convert this small hospital into a large, well equipped, charitable hospital wherein it would be possible to overcome pain and suffering of hundreds of people at very economical cost. During the first discussion, i offered to take care of negotiations of the equipment that the hospital required. That field of negotiation was the strength of my experience in personal business of Wudtools, wherein i used to deal with various countries and suppliers. </p>

<p>This area of influence through participation grew with time in various ways. All the large equipment’s including MRI, C.T. Scan, Pathology equipment’s, Cath Labs, Sonography etc. were all negotiated at very favorable bargain with my participation in their finalization. Starting with equipment purchases, my relationship with Dr. Bharadia grew into intimate family relationships. Dr. Bharadia, when he visited Bombay stayed at my home. During that period he learnt various breathing and energising techniques from me that he religiously followed in his daily practice sessions at Latur. He tells me often that he goes into very minor details of energising each part of the brain and the body by focusing his attention on the part. This is his special ability because he knows the anatomy of the body and the brain as an MD physician.</p>

