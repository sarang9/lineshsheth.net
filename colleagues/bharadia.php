<p>Earthquake at Latur was a trigger for undertaking similar projects to provide assistance and support during floods in Mumbai, Bihar, Tsumni Tamilnadu, tragedy in Kashmir valley etc. We both could provide services of highly equipped ambulances, doctors, medicines, and emergency provisions through Vivekanand Hospital, Latur. </p>

