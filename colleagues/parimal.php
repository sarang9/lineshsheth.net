                  <div class="col-lg-9" style="background-color:#fff; color:#000;">
                    <h3 align="center">About Parimal Merchant</h3>
                    <div align="center">
                      <img style="height:250px;" class="img-responsive lsshadow" src="../images/parimal merchant.jpg">
                    </div>
                    <br>
                      <div style="text-align:justify; font-size:16px;">
                        



<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/LaComunidad.jpg" class="img-responsive">
</div>

<p>Parimal Merchant and me met in the meetings of Community for Human Development around the year 1981. The group of The Community was then oriented by late Mr. Arvind Bhuta.</p>


<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/logo_eng.jpg" class="img-responsive">
</div>

<p>Over years, we both had long association of working together. The life was characterized by eventfulness. Constant learning was conducting thread. Experiential learning came about through joint activities and campaigns of Humanist Movement. And comprehensive learnings came from Meetings, seminars and retreats of humanist doctrinary materials. Both the learnings strengthened our convictions on how human life can become meaningful.</p>

<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/unity.png" class="img-responsive">
</div>

<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/touchstone.png" class="img-responsive">
</div>

<p>Parimal says what attracted and made him stay with The Community for Human Development was the light that these humanist studies threw on the internal world of human beings. They clarified the way out of maze, the complicated paths, of life. They emphasized the importance of being essentially non-violent in life. It amply clarified that the path of happiness invariably demands reconciliation, avoidance of contradictions in life and always to moving towards unity, not towards divisiveness. In a simple message, it said, "Good is what unites people, bad is what divides them. Good is what improves life, bad is what destroys it". As a result, he says, he experienced a distinct level of internal growth, as our association continued.</p>

<div class="col-lg-4" style="margin:10px 10px; padding:0px;">
<img src="../images/story.jpg" class="img-responsive">
</div>

<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/pi.png" class="img-responsive">
</div>

<p>With Parimal, we worked in teams and also individually. What i found refreshing in him was his openness to ideas and his penchant for new points of view. He had a conviction that human beings learn more by stories and examples than by teachings. I found him bold, courageous, and willing to work together and learn. We developed together several methodologies, like Technology of Fulfillment of Action (TOFA) to explain the <b>power of images</b>. We created many stories, anecdotes, examples and ways of explanation that made our teachings illustrative. They helped People understand the humanist doctrine in its usefulness for life.</p>

<div class="col-lg-4" style="margin:10px 10px; padding:0px;">
<img src="../images/fmbparimal.jpg" class="img-responsive">
</div>

<p>In Humanist Movement, Parimal is seen as a reference, as a man with refreshing outlook and as an orientor to bind people with convincing points of view. </p>   

<p>Eventually Parimal joined, as a professor in S. P. Jain Institute of Management & Research (SPJIMR), Andheri. He rose there to top level of management. He showed his strength, through his very well attended lectures, the complexities and the potential of Family Managed Business. He did this not only at institute, but also on invitation to groups of several business communities which were traditionally run by family managed businesses. At the same time, he remained a very sought after consultant in several business houses.</p>

<p>Parimal says, throughout his teaching methods and  explanations, what he was able to convey convincingly to people was the very essence of insights and wisdom he gained from his grasp of the Humanist Doctrine.</p> 

<p>Rather than a scholarly man in his profession, Parimal is more known as a man of wisdom, conveying essence of what unites families and people, what reconciles, what makes conflicts among families disappear, what resolves  contradictions and apparent oppositions among human beings. It was all encompassing teachings of the humanism that he was able to bring home to the hearts of the people.</p>

<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/gpstracksicon.png" class="img-responsive">
<label>Evolution of Life means Ascendance</label>
</div>

<p>Parimal understands that the essence of Silo's teachings, the conducting thread of human race, from time immemorial, is that human life is basically evolving from simplicity to complexity, from complication to clarity and from ignorance to enlightenment. Therefore the future of human being is towards ascendance, comprehension of meaning of life, leading towards a bright future. Therefore the purpose of human life lies in learning to overcome suffering and contribute to welfare of humanity, not in being narrowly centered just in one's own welfare. He says, he understood that a tree grows not only from the nourishment coming from its roots, but also from contributions of each and every leaf as well as its branches. In exactly same way, the Tree of Human Life also evolves by contributions made by every human being.</p>

<div class="col-lg-2" style="margin:0px 10px 0px 10px; padding:0px;">
<img src="../images/fer.jpg" class="img-responsive">
</div>

<p>When asked, what and who contributed most in his life until today, he clarifies that Silo was a distant figure to influence him but Fernando carried the essence of Silo's message both in his teachings and behaviour to such an extent that his association influenced him most.</p>


<p>In the same breath, he appreciates his association with me, my work on images and on science of energy that i expounded, based on Silo's work and his methodologies. He mentions that my single mindedness in pursuing the purpose of Humanist Movement influenced him a lot.</p>

<div class="col-lg-2 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/drManu.jpg" class="img-responsive">
<label>Dr. Manu kothari</label>
</div>

<div class="col-lg-4" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/drShrikant.png" class="img-responsive">
</div>

<p>However he says that <b>Dr. Shrikant</b> of S. P. Jain Institute of Management & Research had a towering influence on him. He saw in <b>Dr. Shrikant</b>, a unique combination of Gandhian upbringing and American education of management, while running an MBA college. Dr. Shrikant exemplified great values of life and deep commitment to a cause. Another man who influenced Parimal's life was <b>Dr. Manu Kothari</b> who carried profound dimension of life through being socially active, through reaching out to others unconditionally, bringing reconciliation through lightness of life, and through peace, force and joy.</p>

<div class="col-lg-2 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/nailesh.jpg" class="img-responsive">
<label>Nailesh Dalal</label>
</div>

<div class="col-lg-2" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/jayesh.jpg" class="img-responsive">
<label>Jayesh Shah</label>
</div>

<p>Among the colleagues, he says he was influenced by total commitment and devotion of <b>Jayesh Shah</b> in bringing order, organization and networking of NGO relationship in Humanist Movement and its organs. He also speaks highly of people like <b>Nailesh Dalal</b> who always reached out to people, brought in reconciliation and remained socially active, while remaining central to his extended family.</p>

<p>Towards the conclusion of our discussion, i asked Parimal to synthesize the present scenario wherein it has become trendy to look spiritual, to meditate for peace, to be liberated from stress of this birth etc.</p>

<div class="col-lg-3" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/me.jpg" class="img-responsive">
</div>

<div class="col-lg-3 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/self.jpg" class="img-responsive">
</div>

<p>He said there is a misguiding dimension of the present trend of spirituality. It is centered on marketing the benefits that one will receive for himself without contributing to welfare of others. It has a deep utilitarian aspect, a consumer like attitude towards the divine, of buying peace for oneself, getting <b>Moksha</b> for personal self, without reference to welfare of the humanity. Most important thing about this version of spirituality is that it is "I" who is in center. It is "My" peace and "My" own personal liberation which are central to this spirituality. All practices are finally contracted, narrowed down, to benefit oneâ€™s personal self. The others in the world are not in picture.</p>

<div class="col-lg-2" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/break.jpg" class="img-responsive">
</div>

<div class="col-lg-3 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/reach.jpg" class="img-responsive">
</div>

<p> Against this, he says the purpose of human life is to selflessly reach out to others, to lose the "otherness" of the other. Your spirituality lies in accepting others, in improving them, whoever they are, without discrimination and without self-centeredness. Without this dimension, the so called spirituality is a form of regression, not of progression. </p>

<div class="col-lg-2" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/up.jpg" class="img-responsive">
</div>

<p>Years of association with Parimal Merchant has been learning, uplifting and enlightening experience. I owe a lot of gratitude to him in my life which is described in a wooden plaque i gave him years back. I reproduce it here as a memory.</p>







               


