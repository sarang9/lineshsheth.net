<p>I met Viren Shah in the year 1980, when he was working with Larsen and Toubro at Powai. Lot of water has flown down the Ganges since we met. I see him even today as an upright, principled and a disciplined man of action, innovation and outreach. To act with innovation and to create Humanist themes with creativity is Viren's strength.
</p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img  src="../images/commitment.jpg" class="img-responsive">
</div>
<div class="col-lg-4 pull-right" style="margin:10px 10px; padding:0px;">
<img  src="../images/facebook_1487833936674.jpg" class="img-responsive">
<label>Viren with Silo and Fernando</label>
</div>
<p>I worked with Viren as an orientor for sometime, as an associate for another period and as a colleague for quite a while to produce translations of Silo's books. During all this time i was constantly amazed at his quality of perseverance and follow up to complete the job that was taken on hand. It was this quality of creativity and perseverance in him that worked behind the industry that manufacture water park and playground equipments that he founded in 1985 in the name of <b>Arihant Industrial Corporation Limited.</b> (<a href="http://www.arihant.com/" target="__blank">www.arihant.com</a>) </p>
<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
<img  src="../images/IMG-20160909-WA0024.jpg" class="img-responsive">
</div>
<p>When i asked him about what drives him to complete every job he undertakes, he told me "My father taught me that when we give 100% to any job we undertake, it is called committment. And when we do not complete what we commit to ourselves, it is self betrayal". </p>
<p>He says, "I am conscious to do what i say and i say what i can do." Viren's commitement to himself, to his family and to the humanist ideology he pursues is unconditional. For him, to be unconditional means, for example, "to smile to life, not if others smile, but to smile even if others do not smile." That stand of unconditionality in favour life gives meaning to the way we live.</p>


<p>I worked with Viren as an orientor for sometime, as an associate for another period and as a colleague to produce translations of Silo's books for quite a while. During all this time i was constantly amazed at his quality of perseverance and follow up to complete the job that was taken on hand.</p>
<div class="col-lg-2" style="margin:0px 10px 3px 0px; padding:0px;">
<img  src="../images/SpiritualTitle.jpg" class="img-responsive">
</div>
<p>Viren understood humanist way of thinking as if it is inherent to him. He says its principle of non-violence attracted him because it tallied with his Jain religion. He liked 
immensely the aspect of internal work proposed by Silo. That work clarified what is internal resistance in us - the force that does not allow us to except new proposals in life -, what is contradiction and what is the importance of coherence in thinking, feeling and acting. That further clarified the force of internal unity within, that allows one to work hand in hand with others in a co-operative world.</p>
<div class="col-lg-4" style="margin:10px 10px; padding:0px;">
<img  src="../images/facebook_1487834229247.jpg" class="img-responsive">
<label>Typical Humanist Meeting</label>
</div>
<p>It was this clarity of his understanding that inspired him to constantly reach others to teach them the unitive way of life and to overcome what prevents the unity within us. Viren started functioning independently, forming his own group, just within 3 months. He had a passion for simplifying the complexity in Humanist teaching. Therefore he constantly made new materials with different points of view. To spread these simplified points of view, he unfailingly held weekly meetings, seminars and retreats in schools, in villages and at Flyover apartment in Andheri. </p>
<div class="col-lg-4 pull-right" style="margin:10px 10px; padding:0px;">
<img  src="../images/kailash.jpg" class="img-responsive">
<label>Kailash Mansarovar View</label>
</div>
<p>One major incident that showed his internal strength, clarity and an energy for not giving up things, was evident when his young elder daughter was swept away while she was at a sight seeing trip at Kailash Mansarover in August 1998. In those moments, I saw Viren extremely steady, persevering the search on internet and even visiting the Kailash Mansarover, trying his utmost for a rescue. He tells me that if it was not for Silo's teachings he imbibed, perhaps he would have collapsed under pressure. He says he feels immensly grateful for the internal strenghth that Silo's teachings gave him.</p>
<p><b>Viren says, during the period he faced above difficulty, he never lost hope. What helped him was Silo's message of reconciliation with life.</b> I was witness to the way he stood during this tragedy. I can say that the true character of Viren showed the way he bore this suffering in his life.</p>
<p>Even today, when faced with difficult situations, he contemplates on "How can i make it possible and surpass?" invariably, he says he receives an answer.</p>
<p>Knowing viren has been a blessing a great learing and an immense pleasure. </p>













               


