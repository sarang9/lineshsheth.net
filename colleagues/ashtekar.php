                    <br>
                      <div style="text-align:justify; font-size:16px;">
 


<p>Prahlad Ashtekar met me when i was in school in Latur during 1950. My penchant for Indian classical music led me to a local music devotional group called Guruvar Club, Ashtekar lived in Latur. He used to play tabla voluntarily in Guruvar Club, attended by local people. My age in those days would have been around 12. Ashtekar was senior to me but in Guruvar club at Latur, i developed liking for him as a very honest person. </p>

<div class="col-lg-3" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/sdk.jpg" class="img-responsive">
<label>Ustad Shaik Dawood Khan</label>
</div>
<p>In 1955 i shifted to Hyderabad for my engineering graduation while Asthekar went to Hyderabad to become a professional tabla player under rigorous training from renowned <a target="_blank" href="https://en.wikipedia.org/wiki/Shaik_Dawood_Khan"> Ustad Shaik Dawood Khan </a>. Under Dawood Khan he rose from a common village tabla player to accomplished artist nuanences of his other tabla gharanas.  </p>

<p>After being an engineering graduate i came to Bombay in 1963. Around 1975 i was living in Santacruz in Archana at Tagora Road. I daily used to travel by train to Mumbai for business. It was during my rush to station among crowd of people in those days that i suddenly noticed a tall figure who looked like Ashtekar. By an instinct, i called his name and he turned back. We sat in a hotel and he told me his miserable situation. He said he was allowed to sleep in a temple in exchange for playing Dhol during Aarti. Suddenly the memories of Latur and my gratitude towards him filled me with an obligation to intervene in his financial situation. </p>
<div class="col-lg-3 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/singing.jpg" class="img-responsive">
</div>

<p>Ashtekar was in difficulty, had to live away from his home. He was dependent only on tabla playing jobs and did not want to deviate from the profession of tabla playing. I decided to put some classified advertisements for him in Times of India from which he got some tuitions. Additionally i asked my elder sister Vasanti, living at Juhu, to train Nailesh, her eldest son, to learn tabla as an art. That assignment became popular in Juhu scheme. As a result, he got several assignments for tabla teaching for students in very well-to-do families. His financial conditions, his appearance, his confidence and his posture changed as if he had found fulfillment and dignity in his life.</p>

<p> At that moment he wanted to shift his residence from his slum from Teli Galli at Andheri. I instantly searched some buildings under construction and was fortunate to be able to buy a one BHK apartment for him in Bussa Apartment at Dattatray Road in Santacruz west. </p>

<div class="col-lg-3" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/girl.jpg" class="img-responsive">
<label> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Sheetal Ashtekar</label>
</div>
<div class="col-lg-3 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/ashtekarFamily.jpg" class="img-responsive">
<label> Ashtekar with Wife Priti & Sheetal</label>
</div>
<p>Ashtekar got remarried to Priti in 1981 and had a daughter called Sheetal, now 32. When Sheetal was small, Ashtekar wished for her an education in English. Fortunately it was possible to get a free entrance to a Christians Girl's school in Santacruz . Sheetal grew to become a graduate in communication over time. She is now married happily in Chembur in a good family and works as a HRD executive. </p> 
<br>
<div class="col-lg-4 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/fampic.jpg" class="img-responsive">
</div>

<p>Ashtekar was uneducated, very honest, and hardworking and without any addictions that nearly always went with renowned artist of those days. He used to be very punctual in his timing. All this endeared the people who hired him for tabla teaching. Among those known to me, Nailesh became an accomplished tabla player. Nailesh liked Ashtekar and Ashtekar had great faith Nailesh's tendency to take care. Ashtekar accompanied various classical instrument players and singing masters including Kishori Amonkar. He accompanied Rasiklal Andharia whenever he visited Mumbai for his concerts. </p>

<div class="col-lg-4 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/gp.jpg" class="img-responsive">
</div>
<p>During his lifetime, Ashtekar and me spent very interesting and heartful time in conversation at my home. My home was his constant place of visit. However during his last days, when he developed cancer of hip, he was greatly taken care by Nailesh. Nailesh arranged some Gurupurnima functions and received a fair amount of money as Gurudakshina, which have stood in good stead through investments in share market by Nailesh. Ashtekar died in 1997. </p>

<p>Ashtekar left indelible impression on Nailesh. He was a great companion for me and Navin Parekh. Both me and Nailesh are happy that Ashtekar's coming in our lives has turned his family's fortune for good. We both miss Ashtekar's humble and artful presence in our life, while we cherish our days we passed together.</p>





