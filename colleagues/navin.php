<p><b>Inspiration behind this musical initiative was Navin Parekh. Navin was a man, whose very presence in a mehfil (musical get together) carried weight. The singer or the instrument player would very easily find in Navin a great participant with whom they resonated. Navin, for me, was an indispensible companion. Without him i will feel alone. </b></p>
