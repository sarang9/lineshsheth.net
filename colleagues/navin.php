<p>Navin Parekh and me married on the same day, 3rd February 1963, and at same venue, along with my brother-in-law Harsukh Parekh, who happened to be Navin's brother. So we were 3 couples married together on one stage. Over a period of time Navin and me found a common interest in Indian classical music- i was a keen listener whereas Navin was a profound knower of music. Besides music, me and Navin shared lot of oneness, harmony and good feelings. </p>

<p>Classical music was an intense love of my life. I dreamed always that great singers would one day come my home and sing or play music. That dream got fulfilled by a simple and personal approach to great musicians like Hariprasad Chaursia, the legendary flute player, Jitendra Abhisheki, a great composer singer and marathi stage artist, Prabhakar Karekar, an award-winning Indian classical vocalist of Agra and Gwaliar gharana and expert marathi Natyasangeet singer, Rasiklal Andharia, a melodious singer from Gujarat, belonging to Kirana and Indore gharana. I approached each of them personally, as a fan and a follower. When i met most of these great artists simply and affectionately during their concerts, to my surprise, they all agreed to come to my house simply on request and virtually without any payment. </p>

