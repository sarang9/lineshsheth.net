<p> I should make a particular mention here of how i met the great musician singer from Gujarat Mr. Rasiklal Andharia. Once i heard Rasiklal Andharia over an early morning radio program from Kutch, Bhuj. I instantly felt that i should invite him to my home to sing, wherever he was. Navin knew Rasiklal Andharia more closely by name and told me he lived in Bhavnagar, Gujarat. </p> 

<p> Not knowing how to contact him, without any common link, i wrote a postcard to Post Master General, Bhavnagar. I asked the Post Master to reach the message to Rasiklal as my personal request. It so happened that he knew him personally. He handed over the message at his home. Everything became easy as he phoned back. I asked him how i would recognize him when he arrives. He said, on coming Sunday, there will be only one man with black Modi jacket Tanpura in his hand, getting down from Gujarat Express at Bombay Central station. That was enough. We had then onwards several common occasions with Rasikbhai with Akashvani Radio Station, Bombay Doordarshan, and with common friends for arranging his vocal concerts. </p>
