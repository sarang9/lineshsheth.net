<p><br>I came to Mumbai from my village and met you on 14th March 2007. I had come for only one day and was employed as a personal driver immediately in SOIL -The School Of Internal Literacy.</p>
<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/honor.jpg" class="img-responsive">
</div>
<p><b>Working here I learnt that SOIL was voluntarily dedicated to humanity to make people discover what is of great value in their innerself. To learn to recognize, appreciate and accept what is valuable within oneself is the beginning of giving meaning to life and moulding it in direction of what gives substance to it.</b></p>

<p>At that time my age was 19 years and education was only upto 11th class in Marathi Medium. My English was poor.</p>
<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/turningpoint.png" class="img-responsive">
</div>
<p>Today I consider this day as <b>Turning Point</b> of my life. For, until then, I had never imagined i could learn english or accomplish an expertize in computerization or even learn how to practice.</p>

<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/we-name1.jpg" class="img-responsive">
</div>

<p>Somehow working in SOIL, my potential was recognized. I saw that i was one of the many whose life was oriented, sponsored and shaped by SOIL. I was guided and supported to learn English and simultaneouly a computer basic course.Then i was trained in english speaking and writing course. I got more confident and i started liking english as a language and computer learning as my future.</p>

<p><b>In 2010 Soil helped me to take an extensive course in Hardware and Networking.That changed my level of inner strength, i became well versed in the field and started teaching this to others.</b></p>

<p>Well versed to handle computer nitty gritties, i started working as an office executive. During this work, I realized that my potential lies not in doing simple manual tasks but in learning and perfecting that which is complex and high-tech. I started investing into time that nights gave me. So during nights, after work hours, <b>i started learning Video Editing.</b></p>
<div class="col-lg-2" style="margin:0px 10px; padding:0px;">
<img src="../images/video-editing.png" class="img-responsive">
</div>
<p>After Video editing was well learnt, I faced a difficult moral choice - I must leave SOIL which supported me throughout, to exercise my freedom of option to move to world of higher opportunities. To my surprise, I was in fact encouraged by Soil to go ahead. In 2011 I joined <b>Lucia Entertainment</b> where i started editing of <b>Marathi TV serials</b>.</p>

<p>I moved further to a firm known as <b>720 Ten Films</b> who were in business of corporate Audio Video presentations for very renowned groups in which film editing was very professional.</p>
<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/fcpx.png" class="img-responsive">
</div>
<p>Through out this period, i had two mentors: In orienting my inner world,it was Soil and its inspiration. In guiding my career in the outer world, it was Mr Ravi Tomkoria, the upcoming film editor. That introduced me to its larger version-the Film Editing. In January 2014, Mr Ravi introduced me to very renowned Anil Kapoor Films where today I am working as an Assistant Film Editor for the new film entitled Khoobsurat wherein i will get officially the credits on the large screen. Eventually i was a <b>Assistant Editor</b> in - <b>Udata Punjab, Rustom, MS Dhoni, Nam Shabana, Toilet Ek Prem Katha. </b> This year, I will complete my Graduation in Commerce. I have additionally started working with 8 time National Award Winner Editor Mr A. Sreekar Prasad. <b>I consider this an honour in my life.</b></p>

<p><b><i>Thanks to SOIL and its orientors,</i></b> in just six years, from a village boy to a non-discript car driver, to an Asst Film editor, <b><i>my journey has seen an ascending inner graph.</i></b> I experienced more mental clarity, rise in energy levels and rise in my spirits.I have learnt, more by example, than by teaching, that giving in life is to centre ourselves in others,to see future even in those that look ordinary,humble and uneducated.</p>
