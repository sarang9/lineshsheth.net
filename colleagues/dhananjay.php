<p>I learnt that receiving makes me hungry for more but giving enlarges my world and gives meaning to the way i exist.</p>


<p>I experienced that besides money, what is very meaningful in life is to give recognition and appreciation. I realize that what elevates a person is encouraging, inspiring and guiding.</p>

