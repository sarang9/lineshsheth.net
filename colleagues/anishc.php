<p>Anish saw in Amit a sort of a co-traveler who also had different insights and vision of future. Anish says he found in Amit, an aggressive and progressive businessman, a great savior, a friend and a hope. He says Amit compensated a lot with his personal strength and ways of working. Many times different points of view arise but Anish sees them as emerging new insights of future. 

Anish says he was inspired and borrowed lot of features from my life and ways of working with creativity. He says, he learnt from me how to get interested in different fields like medical, musical, spiritual, literacy etc. He also learnt how to enlarge the circle of friends and understand how to maintain it without a breakage with anyone. He found that accepting others for what they are, letting go of what differs and seeing a positive in others is a invariably a great trait, useful as a way of life.</p>
