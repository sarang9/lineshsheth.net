<p>Until this development, Anish found himself cornered into technicality of things, not in economics of business. At this stage it was entry of Amit, his younger brother, specialized as an MBA in Family Managed Business, trained by Parimal Merchant, that he found a real companion to wriggle out from constraints of technicality into economics of business.</p>

