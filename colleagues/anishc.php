<b>
<p>By manoeuvring Anish understood that image precedes action. As is our image, so is our action. </p>

<p>However images are not static in our minds. Whatever way they are, formed at a given stage, can be changed. Small images can be made bigger, dull images can be made brighter, image that looked at a distance can be brought nearer, weak images can be made powerful and negative images can be transformed into positive. He realized that this capacity to transform images really transforms one's habitual nature and his potential. </p>
</b>

<p> The work with images helped him to understand that traditionally our business house had an image of only working with woodworking tools and tools of Alloy Steels. He saw in his image a potential to shift from image of woodworking to image of metalworking. That included working with steel pipes and tubes. Consequently, it opened up a future of working with materials other than alloy steel to High Speed Steels, Carbides, Diamonds and Ceramics. That change of image enlarged the size of business and its potential.</p>

<p><b><i>During my discussion with Eduardo, who is a man with great and varied experience in bringing about international collaborations, mentioned that among the main strengths of Anish is his patience and calmness when he is confronted with a problem that needs a considered solution. He said that the times are changing when traders have to have far greater knowledge about the evolutionary persceptive of the technologies so that the buyers can truly depend on the seller's competence. He said in a futuristic tone that Anish's main contribution in the emerging scenario would be not only to help new generation to adapt to the needs of the business, but also to clarify and make them understand the levels at which new technologies exist and compete at the same time.</b></i></p>

