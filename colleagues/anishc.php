<p>Anish, my elder son, 47, always had a great respect for me. During his education, i had a clear choice in front of me. I wanted Anish to take over the technical dimension of our business in Total Tools and Wudtools as an expert. However i had realized during my engineering education that true knowledge comes not only from higher degrees at college but from practical field experience. Therefore i wanted him not to follow a degree course in engineering but to do a diploma in mechanical engineering with a specialized course in tooling specialization. He exactly followed that. It gave him another kind of strength, clarity and direction.<br><br></p> 

