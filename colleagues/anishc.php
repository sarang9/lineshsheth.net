<p>Anish is very avid, enthusiastic and planned visitor to international and Indian exhibitions. He visited several of these exhibitions in many countries as a way of his life. In each exhibition, he saw possibilities of working with various collaborators in different fields. He studied their catalogues, their range and applications in details. He says that he learnt the faculty of going into details and working with them, while he saw me working with different ideas. 

Anish saw that as a business house, our strength was ideas, images, and building of relationships. Money was not our strength. So he understood that it made a great sense to focus on a small range of tools rather than working with multiple tooling systems like drills end mills, milling cutters form cutters etc. He began to understand that the best area to focus was where there was least competition and no intervention from multinationals. He discovered this area in Technology of Sawing, involving HSS Saws as well as high end toolings in woodworking. As a result, he began to focus on few HSS sawing manufactures as well as catalogue driven woodworking tools like CMT.</p>
