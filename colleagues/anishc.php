<p>In the beginning Anish was working, guided by me. However at the age of 17, in 1987, he joined Humanist Movement under orientation of Atul Jadia and Parimal Merchant. The participation of Humanist Movement inspired him to form his own group to study and work together on social front as himself as an orientor. That empowered him towards independence in his way of thinking, working and planning. The confidence he got in Humanist Movement worked for him in business to grow from dependence on me to independence with confidence in his own ideas and way of working. Therefore the journey of moving towards independence was not an act of rebellion against the tradition but was a result of maturing in confidence. I am grateful to Humanist Movement that it made this transition possible.</p>

<p>Over period of time me and Anish started consulting each other, sharing our ideas experiences and the vision of future. This inspired him to search for new avenues in the world to work with. Anish says that what helped him in this process was learning’s he received from work with Images, that i myself learned from Silo's theory, as practically illustrated in his work on Guided Experiences. He learnt the power of images and possibilities of their maneuvering to what we aim for in life. </p>

