<p>Mr. Chunilal Bhimji Shah, Director of Greentose Private Limited, situated at Dalal street, Fort came as a harbinger of a change of my career early in my life. He was unknown to me. There were no common acquaintances. I met him accidently, one day, by chance in his office. </p>

<p> While working with Batliboi and Company, i had requirement of a V-belt. I happened to see his sign board on an electrical post and i thought i should meet him. In my first meeting, he was unusually helpful asking my name, town, background of education etc. </p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/tc.jpg" class="img-responsive">
</div>
<p>I told him, i had know-how of my line but i lacked a space and money to start my own business. Knowing all this, he instantly offered to give me free of charge, a table and a chair in his office if i wished to start my own business. It was quite surprising that a stark stranger should offer to help a stranger like me start his career in the very first meeting. </p>

<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/bhachoo.jpg" class="img-responsive">
</div>
<p>Resigning from <b>Batliboi</b>, i accepted his offer to start a company called Wudtools from the space he had offered to me. He even offered initial capital of few thousand rupees which was quite a substantial sum in those days. I offered him to become my partner and we started <b>Wudtools</b> as a partnership company. I started working with <b>Jaswant Singh Bhachoo</b> to trade in woodworking tools for plywood and sawmill industry.</p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/cube.jpg" class="img-responsive">
</div>
<p>The business grew steadily but the size of business was uninteresting for Mr. Shah. So we mutually agreed to part and i took over as Wudtools as a proprietary company to shift at Cawaji Patel Street in Fort. It was <b>a small, rented 7ft by 8ft cubicle</b> in which i could accommodate one stenographer and a peon.</p>
<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/we-name.jpg" class="img-responsive">
</div>
<p><b>Wudtools</b> grew steadily. At the same time relationship with Mr. Shah also became more intimate. I used to go to his house for dinner regularly.</p> 

<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/satish-vidya.jpg" class="img-responsive">
</div>
<p> He trusted me so much that he asked me to arrange meetings between his engineer son, Mr.Satish and my uncle's daughter Vidya residing at Latur. Eventually they got married and even today Satish is an intimate part of my family.</p>

<p>After Satish, Mr. Shah took reference of my brother-in-law Mr. Priyakant Dalal and arrange the marriage of his elder daughter Suman with Priyakant's sister's son Mahendra. Eventually his second daughter Beena also got married in the same house.</p>

<p>Looking back to meeting Mr. Shah as a stranger and events that followed our relationship, looks like an eventful coincidence and strange togetherness. </p>
<div class="col-lg-2" style="margin:0px 10px; padding:0px;">
<img src="../images/foundation.jpg" class="img-responsive">
</div>
<p>Remove Mr. Shah from my early life and i do not know how many years i would have still struggled to build a home in Bombay that was the dream of my father. Mr. Shah's entry in my life was the <b>Foundation Stone</b> of my career. My major part of family including cousins are today settled in Mumbai, thanks to early shelter, education and marriage arrangements that my wife, Lina could make possible under trying circumstances. Mr. Shah's contribution in my life is a blessing.
</p>

<p>Mr. Shah left his body in the year 2003. </p>










               


