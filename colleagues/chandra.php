<p>Chandra says, she had a history of previous jobs which were monotonous and routine. It was in Total Tools that she discovered how one's potential is released when responsibilities are given and fulfilled, how facing chanllenges in life gives way to one's evolution and how looking forward to an upward looking life everyday fills one with hope, joy and meaning.</p>

