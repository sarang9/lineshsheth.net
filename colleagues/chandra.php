<p>In her own words, Chandra says, she felt release of stress when i told her that she is in a good company and can look forward to an assured future. I used to talk to her occasionly about humanist approach to life. But it was introduction of Parimal Merchant in our office, where he used to hold humanist weekly meetings, she got a completly new perspective about human life, its potential and meaning. She was a regular participant in Humanist meetings and i have seen a steady growth in her involvement, understanding and perceptive abilities regarding the future of a co-operative common social objective. </p>

<p>In a recorded interview with her, Chandra says that she learnt in our meetings about what an openess of envirnoment does to life. In a era of continuously knowledge pouring atmosphere, it is vital to understand that one has freedom "not to know", what he need not know. Working together for a common objective, without self interest, to better this world lends substance to life. To create a mutually appreciative ambit in working acknowledges contribution of others in life.</p>
