                    <h3 align="center">About Siddharth(Sid) Gehlot</h3>
                    <div align="center">
                      <img style="height:250px;" class="img-responsive lsshadow" src="../images/siddharth.jpg">
                    </div>
                    <br>
                      <div style="text-align:justify; font-size:16px;">
                        



<p>It is not often in life that you come across people or events that become meaningful in few days for the benefits of all concerned. Siddharth Dilipsingh Gehlot is one such rare instance in my life. Although my life is replete with many meaningful coincidences, Siddharth's story stands out as extraordinarily meaningful. </p>

<div class="col-lg-4" style="margin:10px 10px; padding:0px;">
<img src="../images/s.jpg" class="img-responsive">
</div>

<p>It is now about 20 years, around 1997, that i happened to travel in an AC bus from Pune to Mumbai during midnight. Siddharth and we happened to sit side by side. We struck into conversation that eventually became meaningful for life. Though Siddharth was an engineering graduate who said he was a non-believer, uninterested in spirituality and philosophy of life, our conversation somehow attracted him because they talked of meaning of life. After about five hours, we parted at two km distance from his home in Mumbai. We agreed to meet same day at 9 am after taking some rest.</p>

<p>Our first meeting, lasted about 1.5 hours. We spoke of clarification on way of learning by experiences, understanding by feeling what we practice and advancing in life by continuous improvement of our comprehension. Everything was scientific, some of the teachings were special and many unfolded how the life can be meaningful through our actions, experiences and positions we take in front of difficulties.</p>
<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/kiran.jpg" class="img-responsive">
<label>Kiran Reddy</label>
</div>
<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/we-name1.jpg" class="img-responsive">
</div>

<p> A very important contribution of Siddharth in my life was in the process of forming the definition, formulating of practices and in précising the contents of SOIL (The School of Internal Literacy). Before Siddharth came, Soil was only an idea with certain knowhow regarding practices of directing, circulating and elevating energy into body. With the help of Siddharth and another volunteer Kiran Reddy we could give shape to the whole concept of Soil. Although lot of work remains to be done in popularizing Soil, the contribution mentioned here form the most valuable asset.</p>

<p>Siddharth says, now his meaning and purpose in life is to share and teach SOIL meditative techniques to the whole world. He strongly believes if he can be so happy, peaceful and lively because of these practices then they need to be shared with all. By this way, if he can remove suffering from world and make it a happy place, it would contribute to his purpose of Life."
</p>

<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/book.jpg" class="img-responsive">
</div>
<p>Siddharth eventually went to USA for higher learning. After sometime he came back and said, he would like to summarize his experiences and learning’s in a book that tells the story by way of point wise synthesis, progressively in each chapter. During his brief stay in India, a manuscript of book was ready. He asked me to correct, elaborate, modify and print the book. The book was ready in a few months and was aptly called <b>"An Autobiography of A Seeker"</b>.</p>

<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/deepak.jpg" class="img-responsive">
</div>

<p>Siddharth always remained a seeker, not a follower. He always had a <b>"passion for comprehension"</b> that led him to understand deep secrets of life in their roots. He studied various philosophers, spiritualists and guides. I learnt that he had special penchant for writings of Deepak Chopra.</p>
<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/med.jpg" class="img-responsive">
</div>
<p>Siddarth, as was his wont, always contemplated and meditated deeply on things that attracted him. As a result, i realized that over a period of time he had become a man with certain beliefs and convictions. From, them he used to have certain stands about life, about future and about things that are certain to come!. </p>

<div class="col-lg-2 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/rev.jpg" class="img-responsive">
</div>

<p>Siddharth, has a great reverence for LIFE. He believes and practices to live his life to the fullest. Anything less is considered disrespect to life. This attitude creates a passion in what he does that turns into a celebration.
To live a dull and colorless life is a personal choice. Life, wants you to live to your utmost potential.</p>

<div class="col-lg-4 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/meditate.jpg" class="img-responsive">
<label style="background-color:#000; color:#fff; padding:5px; font-weight:normal; line-height:20px;">Meditation is not something that should be done in a particular position at a particular time. It is an awareness and an attitude that must persist throughout the day.</label>
</div>

<p>His practices are engaged in spirituality. He believes in non-dualism. Even though he has a separate time for meditation, he is engaged in outer world as a practitioner. He partakes in all his worldly responsibilities and duties with a meditative attitude. He thinks meditation should not stop or be confined to limited period. It should not be non-inclusive. It should encompass all we do in our life. Everything is Spiritual!</p>

<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/affirmations.jpg" class="img-responsive">
</div>

<p>A particular dimension that needs to be emphasized here is Siddharth's great convictions about affirmations as a constant tool for reaching a desired destination in life.  According to him, affirmations are way of aligning our psyche to the future we have committed to. They should resonate with our intention. They are a powerful tool for transformation. An affirmation is a declaration internally made to oneself. It is an actual act of drawing a line in the sand that declares, “This will be”.
