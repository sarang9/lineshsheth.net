    energy is not enough for body and
    mind to excel in life without causing
    undue stress.</p>

    <p>For one to excel, he needs not only
    far more energy but also its balance
    and rhythm. If the brain becomes the
    focus of balance and rhythm of
    energy, it greatly affects one's
    mental capabilities.</p></li>

