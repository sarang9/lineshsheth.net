<p>I have known Avinash Sharma since his childhood, as he happens to be my daughter's son. I remember, right from early childhood we have liked each other. I saw future in him whereas he saw a teacher and a guide in me. He saw my reflection in his mother Nisha, who followed the teachings of the Humanist Movement practically in daily life. So he also saw his mother as a reference.</p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/drives.jpg" class="img-responsive">
</div>
<p>Avinash says, the period until he did his matriculation was governed by his drives and instincts, not necessarily by what is good for his life. In that, he generally followed the trend that was prevailing among youth of his age. He was no longer told by tradition and values about what he should do, nor he was knowing what basically he wishes to do. Largely he followed what other people do or what other people wished him to do. There was no independent thinking of his own.</p>

<div class="col-lg-2 pull-right" style="margin:0px 10px; padding:0px;">
<img src="../images/gpstracksicon.png" class="img-responsive">
</div>

<p> It was during his college days of pursuing graduation, he increasingly became conscious that the life needs to have a direction. He felt that external pursuit of goals are necessary in life, but balance in life is created only when internally he begins to understand how to transform himself from being static to dynamic, from being stagnant to moving towards energy. </p>

<div class="col-lg-2" style="margin:10px 10px; padding:0px;">
<img src="../images/we-name1.jpg" class="img-responsive">
</div>

<p> It was at the point that he came in touch with me seriously. I introduced him to various methods of SOIL (School of Internal Literacy) in increasing the energy, circulating it, directing and focusing it where one wants. </p>
 
<p>Avinash, by nature, is honest truthful, grasping and implementing man. He liked the sensation that these practices created in terms of feeling and movement of breath, in experiencing the energy flow and consequent sensation of blood flow in the parts he concentrated upon. He liked the Soil's approach of the system of moving from complexity of energy flow to the simplicity of its realization. More or less, these methods found their practical application in daily life, during discussions at family level, during work, and during association with his friends. </p>
<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/energy.jpg" class="img-responsive">
</div>
<div class="col-lg-4 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/energy1.jpg" class="img-responsive">
</div>
<p>Avinash recalls the core understanding of Soil which said, mental states are governed by the balance or imbalance of energy flow, not merely by process of thinking. Remembering this, he was able to overcome his low periods of life, which are common with most young people, by boosting and balancing his energy through breathing, stretching and visualizing the bright future. Avinash learnt well the <b>Principles of Energization, Principles of Breathing, Principles of Energy Loss(Entropy and ageing), Principles of Image</b> and its influence on daily life. All these principles are mentioned here as a separate text.</p>



<p>Avinash says he was largely influenced by the tenets let down by Silo that said, "Image precedes action". Therefore as large or small is one's image, so is the size of his action. He learnt the whole characteristic of the imaging system as illustrated practically by Silo in his book "Guided Experiences". He learnt from me the tenets of images which are mentioned in a separate page here.</p>

<div class="col-lg-3" style="margin:10px 10px; padding:0px;">
<img src="../images/facebook_1481548640655.jpg" class="img-responsive">
</div>

<div class="col-lg-3 pull-right" style="margin:10px 10px; padding:0px;">
<img src="../images/cfa.png" class="img-responsive">
<img src="../images/gymlogo.jpg" class="img-responsive">
</div>


<p>Avinash basically is studious and intelligent. With great perseverance and attention he completed his CFA. He is also a sports person, deeply interested in playing football, interested in cricket, gymnasium and fitness. Therefore discipline, regularity and adherence to what is of value in life, family or in work, go together with him as a character.</p>

<p>Avinash is a deeply thinking man. He is quiet, tolerant and contemplative. He says, basically he is convinced that acquiring skill to master the images, mastering the whole system of visualization as also exercises to move, circulate and focus energy are the basis of personal development. </p>
<div class="col-lg-5" style="margin:10px 10px; padding:0px;">
<img src="../images/mean.jpg" class="img-responsive">
</div>
<p>He understands that meaning of life lies in learning from experiencing and in giving to the society the essence of what he has gained. He considers this as a humanist approach to life.</p>

<p>Avinash says, it was very interesting experience to learn that energy can be increased in the body and mind, it can be transferred to any organ, it can be balanced focused and it can be expanded through breathing, stretching and visualization. </p>

<p> In this moment he uses these techniques during his daily train travel, during his work and in those moments he feels low, in order to regain his normalcy. However he is keen to set apart exclusive daily sessions to work on images, visualization and energization. He is conscious in creating an attitude to spread goodness harmony and development to others.</p>

<p>Talking about a particular aspect of his character building, he told me that he is gradually able to form an image of what he is and what he is not. That image of what he wants to be, guides him to reject and not to associate with anything that does not tally with that image he has built for himself. That is his guide, his internal reference, for what is valid for him to adopt and accept in life. </p>

<p>Living constantly with Avinash has been a great pleasure. He has great respect for my wife Lina with whom he unfailingly participates in what is encouraging in life.</p>    

                    




