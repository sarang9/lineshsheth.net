<li><p>We can think of breath as a
    substance or as 'someone' we are
    lovingly related to. However If we
    treat it as a substance, it becomes
    just our living agent for survival.</p>

    <p>If we treat it as 'someone' we are
    vitally related to, it transforms into
    a  healing agent that completely
    changes our way of living.</p>

    <p>The way we think changes quality of
    the way we breathe. If we think
    breath is a substance,  we become
    a consumer of breath or if we think
    breath is what we are closely related
    to,  we become a lover of life who
    smiles as he breathes.</p></li>

<li><p>Our body is not a lump of flesh and
    bones. It is a great web of networks.
    And our breath is a virtual substance
    that carries oxygen to all these
    networks of organs, muscles, bones
    and nerves in the body.</p></li>

<li><p>Oxygen produces energy in the
    body networks. 
    Energy in use is called kinetic energy
    Energy that is stored for future use
    is called potential energy.</p></li>

