<p>Shivlila is married to Vijay Cholkhane. He is an engineer in Maharastra Jeevan Pradhikaran (Water Supply Department). Vijay is very sincere. He fully supports Shivlila and her positive activities. Nagnath, her father in law, likes the whole envirnomnet created today because of activities of The School and popularity it has got in the town of Latur. </p>

<p>Shivlila has two daughters and a son. All three are studying engineering, while the elder one, Prajkata, has completed her engineering in year 2016 and now works in IT company Pune. Shivlila says the culture of enthusiam, optimism and energised living have passed on to the next generation. They all find inspiration in Shivlila and are practicing as much as they can. </p> 
