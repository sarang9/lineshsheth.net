<p>In 2001 my wife Lina got ill and had to be operated in Vivekanand Hospital, Latur. During that period i had to stay in Latur for one and half months. I was looking for converting this spare time into some meaningful engagement. Since i was studying in Latur, i had lots of my school friends living there. I made a morning meditation group with them to teach the new methods about energy science i was practicing. </p>
<div class="col-lg-4 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/parents.jpg" class="img-responsive">
<label>Nagnath Cholkhane with wife</label>
</div>
<p>Since evenings were free, i thought of an old school freind of mine, Nagnath Cholkhane, who was a sign board Painter. Nagnath had a small slum like house on nearly 5000 square feet plot. Nagnath invited few friends from neighbourhood to attend the evening class of meditation. It was during that class that Shivlila, Nagnath's daughter-in-law, decided to join the group to learn the meditation actively.</p>
<div class="col-lg-3" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/shivlilahusband.jpg" class="img-responsive">
<label>Shivlila with husband Vijay</label>
</div>
<p>In those one and half months, Shivlila, 25, got interested in learning and practicing more. She told me that she was running a kindergarden children group in their small house. However she was ambitious and had dreams of building a large school where she can teach three hundred students at one time. </p>

<div class="col-lg-3 pull-right" style="margin:10px 10px 0px 10px; padding:0px;">
<img src="../images/shivlilame.jpg" class="img-responsive">
