<p>As the school grew, shivlila's commitment to meditation and SOIL's energising practices grew further. she says that she gets up at 4 am morning and vigrously practices all the Soil methods of meditation and energisation. These practices have made her more calm, receptive to others and highly energetic through out the day. She says her our outlook towards life has become very cheerful, optimistic and friendly. she looks forwards to her future with brightness. To promote meditation as a system useful to life, <b>Shivlia sponsors regular weekly meditation group of Sri Sri Ravi Shankar in the name of The Art of Living.</b>  </p>
