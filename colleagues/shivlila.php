<p>Shivlila believed in these teachings and her resolve to convert the land where they were living into a school became firm. Bank of Maharastra was approached, who gave a loan of Rs.19.35 lakhs for the project.  Further help from me and Viren Shah, the school got a shape wherein today 300 students get nursery education. The school is called <b>Little Flower Bal Vihar</b>. It is one of the most equipped and decent schools in Latur. Enclosed is a letter from Shivlia, describing these developments in her own words.</p>
