<p>The inspirations and the orientation she received through Humanist teachings told her that: </p>
<p>1. "Thoughts produce action and image precedes action".</p> 
<p>2. A small image can be made bigger.</p>
<p>3. A dull image can be made brighter. </p>
<p>4. More you reapeat your thoughts, stronger you get in your conviction to make &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;them possible. </p>
<p>5. More faith one has in himself, more the effectivity is realization of images.</p>
