<p>Dear Friends of SOIL</p>

<p>Hello!</p>

<p>I first learnt the breathing-led meditative exercises from Soil in 2013.</p>

<p>I was a regular practioner for the first two years. Then I wanted to focus more on making the practice a way of my life. I was seeking a balance between work and myself in my everyday relationship.</p>

<p>I wanted to move beyond just the morning exercise slot and understand how the Soil learnings were impacting my everyday life.</p>


<p>It took me 3 years of conscious learning and effort beore several aspects of my life began to be reformed. I now see that they have affected my life positively.</p>

<p>Net effect of these learnings is that I am unaffected by the so-called 'work pressure' that my generation faces everyday.</p>

<p>My moods ofcourse swing. Hoiwever my responses to anger are under control as I am able to see the bigger picture of focusing on our good and positive actions while analysing our environment in its truest and rawest form.</p>

<p>Another dimension of these learning is that i am now slow to react, rather I believe in improving

my responses to conversations and actions.I have


begun practising regularly for the last one month

again.</p>

<p>My focus is on Soil practices of

1 Brain Diagram and 2. on Smiling Meditation</p>

<p>I make it a point to perform the practices before and after my work-from-home routine everyday. This has kept me balanced and relaxed during this period of lockdown where people are stressed and affected by lack of open spaces and the inability to venture out.</p>

<p>While I would prefer meeting my friends whom cannot meet in person currently, I have still been at peace with the current situation as I have learnt over the years to not spend my mindspace


negatively on things that are beyond my control.</p>

<p>I want to focus on what is in my control and make a better life out of it. I will continue this practice routine till the time the lockdown ends and then learn from my experience how I respond to this routine after the lockdown.</p>

<p><b>Thank you Soil</b></p>
 
<b>Arjun Panwar</b><br>

