<p>REALIZATIONS OF A SMART VILLAGE BOY..</p> 
<p>"Contradiction Reverses Life" </p>
<p>03 December 2019 01:16</p>
<p> Vishal comes from a small district town in Maharastra. Students in small town generally face lot of difficulties. But Vishal was very ambitious, brilliant and sharp.He carried a dream of becoming an airospace engineer to go to Nasa, in USA.</p>
<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
    <img class="img-responsive" src="../images/vishal11.jpg">
</div>

<p>Mukta was a simple devoted girl from his town whom he loved very much.She and Vishal completed their schooling together upto matriculation. Vishal made an earnest promise to Mukta to marry when he parted with her to come to Mumbai for studying engineering.<br /><br />Because of his brilliance and good looks, Vishal became a centre of attraction for many in IIT. During the period of studies, he came in touch with activities of Humanist Movement and studied principles of Internal work from SOIL. In SOIL sessions we studied Silo's 12 Principles that tell us what opposes happiness and what takes us away from conflicts and suffering. Vishal was particularly interested in knowing about life as a growing current that gets reversed when w take contradictory actions, actions in which we internally get divided because we want to possess permanently two conflicting things that oppose each other.</p>

<div class="col-lg-3 pull-right" style="margin:0px 10px; padding:0px;">
    <img class="img-responsive" src="../images/contra.jpg">
</div>

<p>When i asked him why he always talks about this, he disclosed that his college friend Disha, whom he did not so much love as the village girl Mukta, had fallen in love with her.</p>

<div class="col-lg-3" style="margin:0px 10px; padding:0px;">
    <img class="img-responsive" src="../images/disha.jpg">
</div>

<p>Disha was modern, rich and more educated than the village girl Mukta.Her father promised Vishal, education in USA If he married his daughter. The marriage will mainly fulfil his childhood dream of going to USA but will have to abandon Mukta.</p>
<p>What created an internal conflict was that on one hand his heart wanted to be with Mukta whereas his mind wanted to make use of Disha to fulfil his dream of going to USA. He was always attracted by the Principle which said "Contradictory and Unitive actions accumulate within you.If we act according to what unites, nothing can stop you".</p>
<p>Vishal knew he was in contradiction, torn between head and heart, Disha and Mukta. Yet he married Disha and went to USA. Vishal discovered that campus love with Disha was one thing, living together for a whole life was another. Since they had diffrent roots and culture, there were daily conflicts. In process, his dreams of working with NASA lost its fervour.</p>
<p>He now daily dreams of Mukta but now she is married with two children living a content and happy life in her village.</p>
<p>Vishal is back to India, seeks divorce and wants to make a career in his own country.He has one child. Both he and Disha do not know what to do after a broken marriage. Last Saturday, 2nd December, he came to SOIL to seek guidance. We reviewed what he had learnt. Vishal realized from the 12 principles he had studied that:</p>
<p>&bull; If you do not harm anyone, you are free to do whatever you like. He had harmed Mukta, divided himself and enchained himself to suffering, Contradiction is felt when head and heart come in conflict, divides one and one feels torn between two opposing forces. He exactly felt that. </p>
<p>&bull; His life seemed entangled into solving and reconciling personal problems and nothing beyond that.</p>
<p>He asked me if life is only about finding peace in midst of conflicts and struggle or it had meaning beyond the trivialities and a nice career.</p>
<p>I told him that giving meaning to life was never his aim. He was a net receiver in life. He wanted Mukta, he wanted Disha, he wanted to fulfill his personal dreams. He gave nothing to life, nor to people. He never gave respect to his inner "call of heart to marry Mukta and stand like a man of dignity.</p>
<p>As a guide for life, I told him what Silo had said:</p>
<p> "If you think rightly but if your heart is false, it will produce hypocrisy.</p>
<p>If your feelings are true but your reasons are wrong, it will produce stupidity.</p>
<p> If you act rightly with a false head, you will face reversal of action and humiliation.</p>
<p>If you act wrongly, as you did in this case, while your reasons are right, it will leave void, emptiness, ahead of you in your life, And Vishal was feeling as if he was facing truth of his life.</p>
<p>I told him what Silo had stated in Internal Landscape.:</p>
<p>" Go and reverse your contradictory life, reconcile with Disha, love your child and start an intentional new life of good actions, actions that unite and improve other people's life".</p>
<p>" You will then find that world needs you. In process your life will get meaning you have never felt before."</p>
<p>Next week he is going back to USA with a purpose to live a new life.He has asked me to appeal all humanist members and SOIL Students to wish him well.</p>


