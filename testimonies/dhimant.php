<b>Experience:</b><br>

An intense circulation of energy coupled with circulation of blood in the whole Brain stricture was experienced. It was the backbone on which the brain expansion rested . It was shown to me how to visualise and perceive the Expansion of brain on X axis -horizontal ,Y axis- vertical and Z axis- Frontal .<br>

