<b>Practice:</b> Soil Brain March<br>

I am Nitin Kawde, 22, coming from Koliya village near Rajgurunagar, pune., education B.A. For years I have been a Gym practitioner and now a qualified Gym trainer.<br>

I came across teachings of SOIL from what I heard from others who were doing Soil energy practices for sometime. One of the Soil instructors took personal interest in my growth and first taught me Soil Brain March.<br>

The Brain March asked me to march, more or less on the spot. First I slowly and calmly closed my eyes.<br>

After about half a minute, I started the march, lifting both my right leg and right hand simultaneously and go on breathing in from right side until 120 march.<br>

During the march I most importantly noticed that I was constantly breathing from right nostril.<br>

I repeated same process from left leg and also centrally from spine each 120 times before resting on floor for 10 minutes.<br>

I am doing regularly Brain March before I go for Gym practice. My experience is that:<br>

• In Gym I used to workout for 50 minute and used to feel exhausted- more tired than energetic.<br>

• When I preceded it by doing a 12 minute march, including rest, I experienced that I felt very energetic while doing the workout. Initial lethargy I used to feel before start had completely vanished.<br>

• I feel very energetic throughout the day and particularly my local train journey does not wear me out as it used to.<br>

I learnt so far three practices, was personally followed up and I am doing all of them with great interest. I want to affirm specially that everything was imparted without charging or expecting anything in return. I was only encouraged to teach it to others in the same way that I received it.<br>

