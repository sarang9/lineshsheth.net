<p>HISTORY AND MEMORIES OF DR RAHUL YANDE, BEED</p>

<p>31 July 2020</p>

<p>Dear Friends of Soil</p>

<p>I first met Linesh in 1991 when I was studying MBBS at Latur

Association with Linesh & Humanist Moment</p>

<p>I joined Humanist Movement with him. Net effect of being with such a positive movement was that it made me optimist.</p>

<p>HUMANIST MOVEMENT</p>

<p>With Linesh, we conducted opinion polls and invited Silo to meet us at Mumbai. In Movement, we conducted many retreats at

Latur, Solapur, Goregaon etc In our college humanist panel won the election.

Winning the college election We established CDC ( center for direct

communication) in college to conduct different student's activities. We used to take one guided experience in each weekly meeting.

Those days we used to publish weekly newsletter called " Parivartan"</p>

<p>PRINCIPLES AND MAIN THEMES</p>

<p>Our college journal in those days were full of articles. The articles covered Principles of valid actions and how to transform our immediate environment.</p>

<p>Always the main theme was to keep human beings central value of life. In personal life, we stressed on importance of thinking, feeling and acting in same direction to achieve

coherence in life.</p>

<p>We always proposed to think globally and act locally. There was always stress on importance of selfless giving and usefulness of physical, mental and emotional gymnasium</p>

<p>We were fully motivated to Humanise The Earth. For us it was was not just a dream, it was a start of a process to build a world where all can live without violence and in harmony.

After MBBS i studied OSHO. I spent for few days in Osho commune, did different meditation like dynamic, Vipassana and No

mind meditation etc in Buddha hall MY SPIRITUAL TOURISM

when I was medical officer in rural area I met one ISCKON devotee and I started studying Bhagwat Geeta and shrimad Bhagwat</p>

<p>I use to chant 16 rounds daily, reading bhagwatam, doing sankirtan was my routine for almost 8 years. I attained yatras with his holiness Radhanath swami maharaj to south

India, Vrindavan and Mayapur</p>

<p>After 8 years I realised that ISKON became chewing gum. After a little while, It neither tastes nar it finishes.

Meanwhile I was attracted by lecturers of shri Kripaluji Maharaj I met him personally many times

I visited Mangarh several times for sadhana. I cried on many occasions in devotion That experience also did not lasted long.</p>

<p>I met swami Adgadanand maharaj at shakteshgad near Banaras. Stayed for week for sadhana in his association

My experiences could not last longer. REUNION WITH SOIL

While doing all this I was always associated with Linesh. Whenever I used to visit Mumbai !

used to spend one full day with him to recharge myself. Linesh never commented on my search as good or bad

Later I studied teachings of Mehar Baba, Raman Maharshi, Nisargadutta maharaj, Krishnamurti, swami sarvapriyananda. Nothing could satisfy me.</p>

<p>Many of these teachings had contradictions inbuilt in them. As a result, they didn't brings clarity but

contradictory acts accumulated within me.</p>

<p>Again I revisited SOIL. Linesh taught me meditation which is very simple

It is very difficult to describe the experience as experiencing structure absent there.</p>

<p>There is no self that is to be realised I always wanted what those maharaj wanted me to want

My Journey ended where I started. In Soil i realized that "simple is beautiful,

complex is ugly ."</p>

<p>Thanking Linesh is like thanking myself.</p>

