Life was moving on in its ordinary way... A fast and enjoyable college life, followed by a satisfying graduation degree, more entrance exams, more pressure, ultimately leading to a '10 to 7' finance job, which would basically drain my life. As i was somewhere midway in this journey, i was surprisingly diagnosed with a structural instability in my spine. I was diagnosed with a condition which i had never heard of ( spondylolysis), which could potentially take over a major phase of my life, just when i thought i was very much on my rightful "career" path.<br>

As big as a twist this was in my life, i realized that i had no other option but to incorporate a regular physiotherapy regime into my daily life coupled with the biggest sacrifice yet -"Stop Playing Football". To cut a long story short, I had to quit my job, stop going to the gym, avoid football, just rest and exercise.<br>

In this phase of shock, came my calling. My best friend, Arjun started talking to me one day about how he started learning some new teachings from MY own grandfather...Linesh Sheth. I said to myself "Thats it..this is my time..Finally i will have time for someone who i have respected & loved all my life but just never given time"<br>

After a month of my spending some early morning hours with my grandfather, I realised what all I could have missed out on in my life if I had not chosen to learn special energy practices from him.<br>

I already had a basic understanding of the power of the brain & its connection with each and every living cell of the body , but it was only after this fortunate & beautiful time I spent with him, that made me realize that i was living a life which was nothing short of mediocre.<br>

Since October 2013, I have completely changed my daily routine. I realized that the human body is in continuous connection with a higher force, the Cosmos, and this connectivity allows us to download the exuberant energy & force from the cosmosphere into our body, thus revitalizing our entire body. " Energy flows where attention goes" is the key mantra. Its so simple, each organ needs oxygen, blood is the carrier of oxygen, and we can control our blood flow by simply controlling our attention & postures.<br>

I regularly practice exercises termed as:<br>

1) Brain Dynogram ( 140 regulated Breaths connecting the brain with the entire body)<br>

2) Dynatec ( Cumulative Connection of different energy centres in the human body)<br>

3) Network of Networks ( Complete desolution into Mother Earth & revitalization of the body)<br>

4) Intagram ( Flow of energy through every organ of the body)<br>

I have found a new way to wake up in the morning. My day starts with some simple stretches followed by:<br>

1) 2 sets of Anulom Vilom. ( 10 on each side)<br>

2) Microscopic Orbit - Lie down , close your eyes, & feel spirals of energy encompassing each and every phase of your body.<br>

3) Brain Walk. - 3 minutes from each side. This exercise is the easiest one. You are basically walking but your hands move in upward gestures & in a alternate pattern to your legs and all what you have to do is focus on your breath. You breathe in from one nostril and and exhale from the other. This makes walking into a completely different exercise altogether. You feel more fresher than you would after walking for an hour or maybe even more!!<br>

All these exercises take up 75 to 90 mins of my day. But the effects are enormous. I feel active and energetic throughout the day. I feel i am more alert and more receptive when people are talking to me. I no longer feel drained and sleepy in the afternoons. The brain seems more energized altogether. It has a massive effect of my reading and grasping skills. I am studying for my CFA exam in June, & i feel that I am able to study way more efficiently throughout the day compared to earlier times when my study hours would be nowhere near as productive as now.<br>

Basically , i feel there is something missing in my day if i would not atleast do a majority of these exercises. It is just as important as my physiotherapy. People can term these exercises in which ever way-- yoga, power breathing ,etc, FOR me, it is simple...It is a "Way of Energized Living" . Living with a conscious awareness that we are connected to a higher force, and by staying in the connection, we live a life which is anything but "Mediocre".<br>

All my life, I felt blessed to have a Grandfather like Linesh Sheth, but today i am grateful to God that i was blessed with this phase in my life ( thanks to spondylolysis) , because without it , i would be living a mediocre life. Physically, i am not near my desired levels, but mentally, i feel i have reached a whole new level. I am a optimistic and positive person & my way of living has changed.<br>

