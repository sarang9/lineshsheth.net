<p>I came across SOIL through Abhishek, a dear school friend of mine in the year 2013. I am a Law Graduate, 23, engaged in multiple activities that attract me, particularly in the field of student activism and creative development.

Year 2013 was a period in which i was feeling lost, not knowing what to do in life, where to go and what was my identity.</p>

<p>In my first meeting of Soil, the School of Internal Literacy, I was asked a pointed question "whether i felt my life had meaning".! replied, after a moment's pause, -"I think so."</p>

<p>I was told that central theme of participation in Soil was practice ,not just an intellectual question about meaning and purpose of life.</p>

<p>The first practice I learnt was to understand the importantance of learning how the start our day. This was was because the way we start our day, influences our whole day.

Then, during first few sessions,I learnt some of the basic SOIL practices. At first they included Body Stretch, Body Movement, Breath Manoeuvring that teaches how to direct the breath to different parts of the body, Brain Dynogram, Energy Circuits, Dynatec and Integram among others.</p>

<p>I began to integrate with activities of Soil.</p>

<p>Soon I became part of the Soil's research and improvement team. We spent close to 6 months giving definitions and precision to Soil practices to cover in the manuscript of a book.

Upon receiving orientation, I would practice in the morning, and then we would discuss the feedback of the practices, ask and answer questions, clarify doubts, make guidelines, bring about corrections and refinement.</p>

<p>The context of the approach was to work with idea of emergence of a" new man" with new inner contents. In process, we would draw upon the various materials of Humanist Movement, influences from different masters, use the internet extensively and synthesize the learnings.</p>

<p>I gradually started feeling that persistent search in the field of energy was unleashing a creative discoveries in my life.I felt that I am moving towards mastering an important know-how about Energy Psychology. I felt an inner worth that had a weight of a different kind because it had inner substance. I was beginning to know what it is to give direction to one's way of living.</p>

<p>It is now about about 4 years. I am beginning to know what it is to orient life. I am understanding how to influence my surroundings with human values which transform life and to give it a meaning.</p>

<p>As a result of certain practices, that asked me to give divided attention with focus on multiple points, I learnt to pay attention, at the same time, to breath; body; corresponding movements; thoughts;feelings; images and yet remain free from getting involved with them. This practice developed in me an ability in daily life to run parallel steams of work with attention, both professional and personal.

Influenced by the humanist values, I am aligning my personal interests with inclusion of the interest of others. I am taking initiative and have developed capabilities which I practice in the internal work.</p>

<p>My overall experience has been of having lived with a surrounding

that was alive, enlightening and has an emphasis on ascent in life. I

am feeling independent, open to learn constantly, open to adapt,

open to change and to move towards what strengthens life and

gives validity to it.</p>

