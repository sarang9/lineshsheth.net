<b>First day learning:</b> 14.10.2015<br>
<b>Practice:</b> My encounter with Soil Mediation<br>
I saw several videos illustrating variety of Soil meditations designed with different points of view for a practitioner to choose what he wants to gain from what he does . I am myself a long time devotional meditator.<br><br>
After practically experiencing few of Soil meditations i now feel that, in essence, Soil's way of illustrating and teaching is a well balanced synthesis of several mystics from Zen, Tao, Meherbaba, Yoga, Osho and Yogic philosophies although i do not know how all this is inspired . This is however my personal view .
When i entered Soil premises, i went with a habitual restricted mindset but in a small period of time , my perspective of mind got considerably enlarged in which smallness of my personal problems felt like dissolving .
The particular concepts of Soil i liked are the following :<br><br>
<b>
<li>Soil's way of engaged spiritualism in which one communicates and gives selflessly to lose the otherness of others in the world as against withdrawing from it for personal salvation .</li>
<li>I really liked its height psychology to transcend personal interest in life to create a better world .</li>
<li>Another really relevant and truly applicable concept i learnt was that when we meditate as if we pair ourselves authentically with cosmos, there happens a wireless inter- transfer of data from the cosmos including earth.
  <br>That data is in form of energy, abundance and joy in human body mind structure and back . I found this fascinating and practically applicable in meditations .</li>
</b>
<br>Overall, the whole experience for me was revealing ,enlarging and workable for one's personal growth .<br>
