<p>Dear Friends of SOIL,</p>

<p>I went to my teacher, Mr Linesh Sheth
today, to enquire about his well being.
He seemed a little pale. He was lying
down on the bed, but he said he was
fit enough to teach.</p>

<p style="color:red;">My Question:</p>
<p>I told him that my mind was full of
incoherent thoughts. They created a
confusion. I asked him if he can tell
me more about bringing clarity to
mind, how to think clearly and how to
remain at peace</p>
<p style="color:red;">His Proposal</p>

<p>He said that we should do a
meditation together and see what
happens in about 25 minutes.</p>

<p>After 25 minutes of experiencing,
discovered the following:</p>

<p><b>1.I realised, the chief characteristic of
mind is emptiness. Anything present
other than emptiness is furniture of
the mind.</b></p>


<p><b>2.However, mind always wants to be
occupied. It could be with thoughts,
memories, feelings, fantasies, gossips
or anything. It's happy so long as it is
occupied, because the contents of the
mind, rule the empty space, the mind
itself.</b></p>
<p><b>3.Therefore, for the purpose of having
clarity of mind, to think clearly and to
be orderly, it is necessary that we
regain empty space.</b></p>
<p><b>4.Regaining the empty space of the
mind would mean that we learn how
to replace the old junk or useless junk
with something that is fresh, new
energetic and pleasing.</b></p>
<p><b>5.However, mind is still occupied, but
now it is occupied with newness, as
the old mind is abandoned.</b></p>
<p><b>6. The process of doing this is to fll it
with something that is neutral, yet
ively and refreshing. One thing that
fulfills this requirement and is
naturally available to us is breath.</b></p>


<p><b>7 The important thing is to learn is
how to breathe deeply, rhythmically
and dynamically so that it is the ife
that is born out of breath drives away
the old junk.</b></p>
<p><b>-So this is the first step towards
experiencing a new *mindscape*</b></p>
<p><b>The experience</b></p>
<p>I realized the above when my teacher
asked me to close my eyes all by
themselves without effort. Then he
asked me, to count only the exhaling
breath 30 times as they sink into my
metabolism to reach until pelvis.</p>
<p>When I did this, I felt as if I was
floating. Calmness was pervading
everywhere. I felt very light, as if l am
flowing like water.</p>
<p>Then I was guided, yet with my closed
eyes to inhale 10 times through my
legs till my forehead.</p>
<p>Upon completion, I felt as if a new


burst of energy was charging mne.
When I opened my eyes, I felt not me
but my mind had opened. Everything
that was old seemed to have
disappeared. I was in front of empty
space within me.</p>
<p>Then, I breathed 50 times in sets of
10, with a process called Torus. The
center of movement of breath was my
whole skull. I did this all by
visualisation and without the use of
my hands.</p>
<p>It was an amazing experience. I felt as
if my mind was largely free of any
contents. There was clarity, freshness
and I felt as if I can think in a new way
with openness, clarity and order.</p>
<p>This was a great experience of SOIL
meditation and I feel that this is "<b>THE</b>
MEDITATION" that I should perfect, as
it has a great possibility to unleash my
potential.</p>






