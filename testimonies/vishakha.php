<p>The Principle of Unity 
                & Decision Making.:</p>

<p>Management schools teach us decision making using theories. However, what is at the heart of the process of decision making is beyond theories. </p>

<p>THE MOST IMPORTANT FACTOR IN DECISION MAKING
Importance of Internal Unity</p>

<p>The most important factor is the oneness within. If our head and our heart are being pulled in different directions, we are divided within and that makes it difficult to take a decision. We will regret it whichever way we act because our inner self is fragmented and it will remain a remorse always. </p>

<p>Life becomes easier when we aim for unity within, which means alignment in our head, heart and body. The principle of Unity eliminates the inner conflict and eases decision making such that we are at peace.</p>

<p>The universe is ubiquitous, it is omnipresent such that distances do not matter. It reaches out to us wherever we are. It touches us and flows within us through our breath. </p>

<p>BREATH, AND UNIVERSE AS TEACHER</p>

<p>Our breath is its lively agent. The universe is live and it is seamless, there is unity in its existence. We only need to observe; there are so many lessons to learn from the universe. It is teaching us all the time, question is. 'Are we listening?' </p>

<p>This unity of the universe, the oneness is one of the greatest qualities that we as human beings can learn or borrow from it. Imbibing the oneness of the universe within us brings inner peace. </p>

<p>The elements of the universe also exist in us. We only need to take cognisance and recognise this. Unity of the head, heart and body solves most problems and challenges in our life. Our decision making too is governed by this unity within us. </p>

<p>As long as we are not able to do that, the inner conflict will continue and so will the struggle. </p>

<p>We gain what we pay attention to. Being single-minded and focused brings results that we desire. It's like being Arjuna, who can only see the fish's eye and 
nothing else.</p>

<p>TRIAD OF TRANSFORMATION</p>

<p>Attention is the key and it has 
these 3 dimensions to it that we need to be mindful of.</p> 
<p>•  First is the direction of our attention.</p>
<p>•  Second is the sensation it creates 
   within us and lastly,</p> 
<p>• the transformation that it causes. </p>

<p>This is the Triad  of Transformation. The idea here is not to correct our bullock cart but to introduce a new mode of transport for our onward journey like a car or a plane. </p>

<p>If we observe our gestures when we are irritated or angry, we will realise that our gestures manifest our inner state of being. It determines what is happening within us</p>

<p> We can choose our gesture by being mindful and in awareness. When we design our gestures, we manifest the desired state of mind and being.</p>

<p> Consciously introducing a new pattern of gestures that are uplifting has the potential to transform our way of existence to a powerful being in the driver's seat.</p>

<p>IMPORTANT DIMENSIONS OF DECISION MAKING</p>

<p>Decision making requires unity, balance and perspective. Today, I learnt </p>

<p>1.	The exercise of Brain Dynogram from the point of view of how it helps decision making.</p>

<p>2.	Through guided breath and gestures, we create energy largely in the brain – the left side, the right side and the mid brain (the ida, pinga and shushumna), the channels through which our life force flows within the body.</p>

<p>3. Our hand is the steering wheel of our breath. An essential conducting thread of the Brain Dynogram is the Tendency to Take Time (TTT). </p>

<p>If we hurry, it brings division and breaks the unity. As we move our left and right hand up respectively, we sense more energetic sides of that side of the body.</p>

<p>When we lift both our hands, it is a gesture of lifting and expanding centre of energy.</p>

<p>•  It enables being not trapped by the
    problem but being able to see clearly.</p>
<p>•  It changes our brain capacity. </p>
                    
<p>                    EFFECT OF TORUS 
             PATTERN TO BREATHING </p>

<p>Similarly, the Torus is an attempt or an attitude to move from</p> 
<p>      ▪︎  Isolation to integration;</p> 
<p>      ▪︎ from bring individual to community
          and from </p>
<p>      ▪︎ being personal to universal.</p>

<p>My today's experience showed me that daily practice of Brain Dynogram and 
Torus completely changes</p>

<p>• the focus of the energy in the body,</p> 
<p>• the pattern of energy and</p> 
<p>• order of metabolism. </p>

<p>TIME AND EFFECT</p>

<p>This one practice which normally takes 20-25 minutes does not only bring about alertness but also changes our character over a period of time. It affects our being and identity. </p>

