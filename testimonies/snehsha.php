I am Ms Snesha, 25, a Specialist Photographer and a professional User Experience Designer . I happened to attend one of the Soil future planning meeting. Until then i had some ideas about Soil but no knowledge, nor any experience about it . So I was invited to attend a practice session . Here is what i experienced :
We did one Brain March and one Smiling Meditation. The effect is as follows .<br>

<li><b>BRAIN MARCH :</b> Time taken : 6 minutes</li>
~~~~~~~~~~~~~~~~~~~~~~~~~~~~<br>
After 6 minutes, i relaxed and reviewed what i had done and what was happening : I felt ligher, 100% energized. Felt good about the day.
It was little tough today as it was first time. But i felt more energy in my torso than in the brain,. Legs were more energized than hands .
I think i have found a good way to start my mornings as i felt 'something' in me, that was blocking my expression, had come out.<br>
<li><b>SMILING MEDITATION :</b>Time taken: about 12 minutes .</li>
It was Very relaxing .This dimension was completely new as an experience . I Used to do physical activities for a reason outside and around myself. But this meditation created a deep inner touch with my body ,mind and life process. I never felt so close to myself before.
I think i learnt first lesson of what it is to begin to 'feel' myself. It was only 10 minutes but it was pleasant and profound experience .
Now i like to begin my day with the brain march, followed by the Smiling Meditation. The Smiling Mediation 6 minute video we saw before we began was helpful and guiding .
So far I knew what Soil was about. .But after actually practicing ,experience validated all the things i had heard about what Soil does to a human being by bringing him profoundly close to his ownself.<br>

First We had today a teaching session and you made notes .<br>
Practice we covered is called :<br>
"Upload and Download" consisting of :<br>
<li>Closure of eyes and sustained surrender to earth with merging with earth .</li>
<li>Rising very slowly breathing through spine 20 times anticlockwise, kneeling down to earth and rising again to do 20 clockwise rotation and surrendering to earth .</li>
<li>Rising slowly uploading from earth the creativity of life , it's nourishing qualities and strength and needing those with cosmos above with both hands .</li>
<li>Downloading from cosmos the abundance , peaceful order and it's quality of penetrantion into your body ,kneeling down to earth for second round .</li>
<li>Altogether 5 rounds with same or different upload and download contents .</li>
