<b> My life experience with Soil</b> <br>

I came to Mumbai from my village and met you on 14th March 2007. I had come for only one day and was employed with you as personal driver immediately in <b>SOIL -The School Of Internal Literacy</b>.<br><br>

Working here I learnt that SOIL was voluntarily dedicated to humanity to make people discover what is of great value in their innerself. To learn to recognize, appreciate and accept what is valuable within oneself is the beginning of giving meaning to life and moulding it in direction of what gives substance to it.<br><br>

At that time my age was 19 years and education was only upto 11th class in Marathi Medium. My English was poor.<br>

Today I consider this day as Turning Point of my life. For, until then, I had never imagined i could learn english or accomplish an expertize in computerization or even learn how to practice.<br><br>

Somehow working in SOIL, my potential was recognized. I saw that i was one of the many whose life was oriented, sponsored and shaped by SOIL. I was guided and supported to learn English and simultaneouly a computer basic course.Then i was trained in english speaking and writing course. I got more confident and i started liking english as a language and computer learning as my future.<br><br>

In 2010, Soil helped me to take an extensive course in Hardware and Networking.That changed my level of inner strength, i became well versed in the field and started teaching this to others.<br>

Well versed to handle computer nitty gritties, i started working as an office executive. During this work, I realized that my potential lies not in doing simple manual tasks but in learning and perfecting that which is complex and high-tech.<br><br>

I started investing into time that nights gave me. So during nights, after work hours, i started learning Video Editing. After Video editing was well learnt, I faced a difficult moral choice - I must leave SOIL which supported me throughout, to exercise my freedom of option to move to world of higher opportunities.<br><br>

To my surprise, I was in fact encouraged by Soil to go ahead. In 2011 I joined Lucia Entertainment where i started editing of Marathi TV serials. I moved further to a firm known as 720 Ten Films who were in business of corporate Audio Video presentations for very renowned groups in which film editing was very professional.<br><br>

Through out this period, i had two mentors: In orienting my inner world,it was Soil and its inspiration. In guiding my career in the outer world, it was Mr Ravi Tomkoria, the upcoming film editor.<br>

That introduced me to its larger version-the Film Editing. In January 2014, Mr Ravi introduced me to very renowned Anil Kapoor Films where today I am working as an Assistant Film Editor for the new film entitled Khoobsurat wherein i will get officially the credits on the large screen.<br><br>

This year, I will complete my Graduation in Commerce. I have additionally started working with 8 time National Award Winner Editor Mr A. Sreekar Prasad. I consider this an honour in my life. Thanks to SOIL and its orientors, in just six years, from a village boy to a nondiscript car driver, to an Asst Film editor, my journey has seen an ascending inner graph. I experienced more mental clarity, rise in energy levels and rise in my spirits.I have learnt, more by example, than by teaching, that giving in life is to centre ourselves in others,to see future even in those that look ordinary,humble and uneducated.<br><br>

I learnt that receiving makes me hungry for more but giving enlarges my world and gives meaning to the way i exist.<br>

I experienced that besides money, recognition, appreciation, encouraging, inspiring and guiding are very meaningful part of giving in life.<br>

Thank you for coming in my life.<br><br>

