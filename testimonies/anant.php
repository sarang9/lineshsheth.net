<b>First day learning:</b> 15-Dec-2013<br>

<b>Practice:</b> BRAIN WALK <br>

1. It was my regular trip to Mumbai on 12 and 13th December.2013 After a long while I decided to stay and catch up with Lineshbhai, my best old friend. I know him since years for his unabashedly persistent passion to teach, to insist on experiencing some new breathing techniques every time and to taste fresh life experiences.<br>

But this time, it was so profound . I had never experienced such a thing before.It was very beneficial.This time it was instantaneous.<br>

2. I am a doctor, studied Orthopedics in a reputed Hardikar Hospital in Pune after MBBS. After practicing medicine for 7 years, I dedicated all my time, energy and focus towards creating a hospital for poor and for various social projects for poor rural and urban people.<br>

Since Linesh Sheth knows me well,he asked me that day morning on13th December,Friday, to try something new for 5 minutes.<br>

Reluctantly I obeyed as he was just asking 5 minutes.<br>

The process he asked me to try was like this:<br>

3. Walk for only 5 minutes comfortably. While walking lift your hands up as one normally does but a little higher, one after the another.<br>

Start breathing in (inspiraton) taking right hand up while walking and breathe out (expiration) bringing right hand down and left hand up. Very very simple.Just like we walk regularly but synchronizing it with inspiration and expiration.<br>

The only thing one is supposed to perform intentionally is 'focus your attention on right nostril for breathing in', as if you are instructing your body to breathe only from right nostril.<br>

<b>My experience:-</b><br>

UNBELIEVABLE..! At first, i could not even know what has happened. I just sat down on his sofa and wanted myself to cool down...yes, it was like cooling down really.<br>

First time I felt the real physical experience of presence of brain in my head. There was abundance of activity happening in my head as if energy is flowing and a current is getting circulated.<br>

In spite of being Medical Doctor, I preferred to comment as a lay man that time. I was feeling like the brain is getting recharged. And there was immense pleasure I was experiencing.<br>

4. He then asked me to repeat the same for just 5 minutes with left hand side. Same walk as one walks normally but this time focusing on left nostril. Instructions to body to breathe from left nostril while breathing in (inspiration) while raising left hand and bringing it down while breathing out(expiration). Just 5 minutes..!<br>

<b>My experience:-</b><br>

This time I experienced the same.Same flow of energy in the brain, same hot feeling in the nostrils as well as brain, same enthusiasm and charged experience..<br>

BUT, this time, i distinctly noticed that my left side was more recharged. My left nostril,which was blocked due to cold, opened up and breathing normally.<br>

When Linesh Sheth had asked me after first spell, 'whether you feel any difference between left side and right?' I had not noticed. But after left side spell, I realized, the difference was quite evident, palpably.<br>

Now, by this time, I was more willing to try Linesh Sheth's insistence on experimenting with what is called 'Central Brainwalk' .<br>

5. Central Brain Walk required lifting both hands up together while breathing in and bring them down while breathing out.I was asked to breathe in from back through the spine and breathe out from front of the body. 3 minutes of this kind of walk gave me another kind of experience.<br>

<b>My experience:-</b><br>

This time all above was present but, in addition to it, I felt complete relaxation in my back (instead of exhaustion, which i thought I will experience due to such unaccustomed exercise).Besides, it was as if my capacity to breathe in had enhanced all of a sudden.<br>

<b>Conclusion:</b><br>

In all 3 spells of " Breathe while you walk ", distinct finding and experience was of peace and energy. Every time, my mind was empty, thoughtless and blank. This was very very energizing.<br>

How was my rest of the day:-<br>

That day, I had 8 appointments all over the Mumbai, running back and forth, meeting people, continuously talking to them, creating something new with them.It finally got over at 11.30 in the night and yet, I was not tired neither exhausted.<br>

<b>Synthesis:</b><br>

I think, this is very profound in terms of not just transient feeling or one time experience but beneficial in terms of peace and energizing oneself.<br>

I have started performing the same now everyday with lots of expectations and benefits..!<br>

