<p>Testomony of Torben from Germany for School of Internal Literacy-SOIL</p>

<p>I am Torben, 27, a business administrator. I come from Germany. I got introduced to SOIL during my business trip in Feb 2018. I was attracted by its emphasis on breathing methods to energize. I met Mr Linesh Sheth in my hotel room at 7.30 am when he talked of giving direction to a day as a first step to give direction to life in terms of creating attitude for the day, channelling our energies selectively in different parts of the body and open the doors of our potential by learning to remove internal blockades.</p>

<p>I went through four practices:</p>

<p>• A brief meditation on setting attitude for the day. It included closed eye reflection on acnowledgment, gratefulness and joyfulness for the precious gifts suc as Body, mind, life and a favourable world given free. : Without this awareness, we remain a mere consumer of our gifts.

For me this was a very sensible approach to life and i liked it the most.</p>

<p>• Second practice was called Brain March. It involved directed breathing while marching. While marching, hands were raised from left side of the body, right side of the body and middle side of the body for one minute (30 times) each. Totally minutes of march produced extra ordinary activation in my brain. I could compare it to 30 minutes of basket ball game i used to play.</p>

<p>• The third practice created accumulation of blood in Medulla Oblongata at back of my brain and top of the brain. Additionally i learnt very slow method of circulating the blood in the whole skull. Overall effect was feeling light in the brain and feeling activated.</p>

<p>• Fourth and the last practice was aimed at removal of blockades from the brain to pave way for easy flow of blood, energy and fluids. This tends to increase creativity of brain, remove lethargy and slow down aging process.</p>

<p>It involved intense upward breathing, raising both hands and a new all encompassing Torus pattern of breathing clockwise and anti clockwise.</p>

<p>The whole approach was new to me. I was not accustomed to such disciplined and methodical way of energizing with breath. I value this teaching because it comes from my own experience, but guided by an experienced teacher.</p>

<p>I wish i am able to practice this learning on regular basis in my daily life.</p>

