I learnt here that if we are bound by merely routine of repetitive life, it will stagnate and deteriorate into boredom and aging.<br>

It is when we learn skills to introduce deep quiet, abundance of energy and spirit of elevating life to higher levels that a purpose and meaning enters in it. That changes our image of who we are. With the above in view, l learnt:<br>

