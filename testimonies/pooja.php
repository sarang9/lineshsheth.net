<p>I am Ms Pooja Vasu, an illustrator, visualizer and designer by profession.I came in touch with Soil upon invitation to attend a half day free seminar arranged by Soil on 17.03. 2018. on meaning of

life.</p>

<p>I was attracted because i was always searching how to acquire peace, quietness, joy, energy but did not how to go about getting it.</p>

<p>In process, i attended a course on Vipassana. I felt very limited with its approach. Its description of mind's charactetistics did not help me. I felt that it did not tally with what i thought about myself.</p>

<p>I also remained with a Buddhist Nunnery for over month. I think my base for tendency to meditate developed there as i had glimpse of profound experiences. However, there was a special conducive environment, unlike the daily noisy conditions I always lived in.So it was difficult to reproduce same experience i had in Nunnery, in

day to day life.</p>

<p>INTRODUCTION TO SOIL.</p>

<p>However my search for an alternative approach to life continued. It was in those moments that i came across Soil movement. I list here my

experiences.</p>

<p>• Learning the Cosmic Download</p>

<p>In first session / was introduced to the idea that

our inner states of psyche are alterable at will with energy and serenity produced when we very slowly breathe upwards as if to remain in touch with abundant cosmic energy and then download

the infinite energy within us very very slowly so that it merges with our own energy.</p>

<p>Discovery 1.</p>

<p>The phenomenon is called transfer of higher force within us Cosmic Bluetoothing, which is a wireless connectivity with the downloading source. Just few such breaths completely changed my inner state as compared to what i had before I came to attend the seminar. The experience was first discovery of my potential to control my inner state at will by the change agent called breath which !

was always available as my resource.</p>

<p>Last week when i was feeling in low energy in my

office, i instantly moved to a convenient place,

tried the breathing method I had learnt and to

surprise, felt not just restoration but rejuvenation

of myself.</p>

<p>Discovery 2.</p>

<p>Second discovery came about when i did 15

clockwise and 15 anti clockwise universal

breathing pattern called Torus breathing, very

very slowly, with cosmic visualization of merging

myself with cosmic energy that abundantly

surrounds me. It produced a great oneness,

feeling of harmony and balance that i had always

wished but had never experienced before. I

realized I had within me an inbuilt capacity to

daily produce a coherence and harmony within

myself.</p>

<p>Discovery 3:</p>

<p>Kapalbhati pranayama which consists of short, powerful exhales and passive inhales, was something I used to do regularly as part of my Yoga session.</p>

<p>However, here I was asked to shift my attention

to my skull while I continued to breathe from

stomach. To my surprise, i discovered that just the

shift of attention unusually energized my whole

skull.</p>

<p>Then when i shifted my attention to heart I had powerfully new sensations there.</p>

<p>In process, i discovered that: </p>
<p>• Energy flows where attention goes. </p>
<p>• Blood flows where energy goes. </p>
<p>• Strength flows where blood goes.</p>

<p>My Realizations: </p>
<p>I was hale and hearty. But SOIL teachings have shifted my internal level distinctly. Now i have realized that:</p>

<p>• Routine life is meaningless till it

does not ask what for one is living.</p>

<p>• A self-centred existence does not

allow scope for evolution.</p>

<p>• Unless one learns to practice for his

own upliftment on daily basis, to

remain intimately with one's ownself,

He will internally remain stationary

in life. </p>
<p>• Avoid ordinariness. Always practice not just for health but to be a Master

who becomes a reference for others

to learn from.</p>

<p>IN SUM: </p>
<p>All further sessions of SOIL were full of discoveries based on new practices I learnt. In process, I think I have discovered my new "me". Association with SOIL has been an advanced

education in Internal Literacy.</p>

<p>I have understood the methods of practicing so well that i feel I am ready for teaching these valuable concepts selflessly to others with a spirit of volunteerism. I feel my life will attain a new

meaning in that pursuit. Meanwhile I have started

inviting new people to this movement.</p>

