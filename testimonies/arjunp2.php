<p>It was 2013 when I first heard about Linesh Sheth, whom I lovingly call Nanaji and have referred to him as Nanaji in the rest of this write-up.</p>

<p>I had failed in my first attempt of the civil services entrance examination and was not sure about the career direction that I wanted to adopt in the future.</p>

<p>I happened to go to Nanaji's house. We didn't discuss the failure, but discussed about life, our visions, our thinking approach and most importantly, he taught me 3 exercises.</p>

<p>It was a new experience for me and I not only felt calm but also felt composed in that moment. I knew I had experienced and discovered a new perspective on how I should go about with our lives.</p>

