<p>Testimony</p>

<p>I am Ms Anna CARVALHO from TURKEY,

28 years of Age. Somewhere in my mind i had a deep desire that I should learn meditation, a way to be in honest touch with my innerself. But so far i had neither found time nor teacher from whom i can learn.</p>

<p>Then in March 2018 i found an occasion to be in India for India Wood exhibition in Banglore,

where in, by chance, i came in contact with a teacher of SOIL who instantly agreed to teach some of the SOIL'S meditations. The meditations involved energy building, brain activation and building sacred and authentic relationship with my body, mind and life force.</p>

<p>Venue was Hotel Taj in Banglore. We met on Pool side.Surrounding was pleasant and quiet. My

teacher was Linesh.</p>

<p>• To begin with he told me that Turning Point in life begins with the way start our mornings- a few minutes of loving contact with

each organ of the body.</p>

<p>This was followed by contemplation of feeling grateful to the mind. Finally, there was a meditation to be in touch with the all intelligent, compassionate and formless guide that surrounds us. The invisible guide that always protects, helps to evolve and remains as constant companion we can talk to.</p>

<p>About 15 minutes of meditation changed my inne. state of mind to feeling very warm, integrally connected and loving the life i was given.</p>

<p>• The second meditation i learnt was dynamic in nature in the sense that it asked me to march in synchrony with my breath from left, right and central side for 9 minutes.</p>

<p>At the end of 9 minutes my whole body felt energetic, fresh and dynamically at ease. I felt as if i had opened myself up. Energy I felt could be compared to 60 minutes of Gym work out, feeling more pleasant rather than exhausted.</p>

<p>• A very useful new practice i learnt was to regulate an intense blood flow to Medulla

Oblongata and to the top of the brain followed by blood circulation in the entire skull. This particular experience was quite different than what i experienced with Brain March. In this i experienced a sense of balance and fullness of

energy</p>

<p>• Lastly i learnt a very interesting way of expanding my psyche and the sense of being universal movement of energy by following Torus

through mimicking the

pattern of three rounds.</p>

<p>Meeting Soil has not been just accident, it appears as a very meaningful intervention of a greater intelligence in my life.</p>

<p>Now it appears that if you fervently ask and deeply desire something, destiny arranges things to happen. When an opportunity presents itself, i would like to teach it to others and would seek a longer period to learn the whole science.</p>

