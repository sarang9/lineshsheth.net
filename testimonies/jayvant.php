<b><br>Problem: PERIPHERAL NEUROPATHY</b><br>

<b>Symptons :</b><br>

1) Loss of sensation in both my feet . Gradual onset of numbness . Pin prick Could not be felt unless pressed very hard.<br>

2) Feet could not feel the grip on the ground causing to lose balance Occaisonally . Could not stand in the same position for a longer time Felt Pain or stress . Stiffness in ankle and knee joints and felt as if some heavy weight chain tied to my legs while walking . Pain or fatigue in feet while sleeping at night . sometimes felt low back pain.<br>

3) Diagnosis : My friend advised me( after a net search ) that symptoms were similar to Peripheral neuropathy My personal physician had advised me to walk regularly and my Physiotherapist advised me to perform knee bending exercise and rotate fee Clockwise and anti clockwise from help much .<br>

Coincidentally I met my close friend and a philospher ( Mr Linesh sheth ) to whom I narrated my problem and he recommended some exercises to be Performed now with a difference .<br>

He demonstrated the new concept of conscious and rhythmic breathing while directing the energy towards specific parts of your body . ( later on termed the concept “ Breathing Gym".<br>

Around the same time I met my relative in Mumbai who taught me an exercise that will help me to reduce my low back problem.<br>

I introduced the concept of conscious breathing in this exercise as well on my own . I have been performing these two excercises for over last three years with tremendous results .<br>

<b>Results and improvements:</b><br>

a) The first positive results were felt within three months . Could feel life coming back to my fingers first and slowly to other parts of the feet .<br>

b) I could also feel freeness in my knee joints .<br>

c) slowly I regained senses in other areas of my feet and felt tighter grip on the floor today, almost after three years plus, I have regained senses in almost the entire feet of both legs. Numbness has gone.<br>

d ) Ankle area has seen lesser improvement compared to other areas in terms of freeness . It is a lot more freer then initial stage . still feel heaviness in this joint.<br>

e ) Better grip on the floor . Can now stand on a spot for a longer period. Better Balance .<br>

The two exercise are narrated step by step for benefit of others with similar problems.<br>

<b>Exercise No 1 :</b> Step by Step<br>

1) Lie flat on your back with each hand on your side .( Savasan)<br>

2) start conscious breathing from your toe upwards first left side and then right side each five times as narrated below …….<br>

• Start conscious breathing slowly from left toe upwards through calf , knee Pelvic bone , left side stomach / rib cage moving upwards toward your Shoulder joint , hand elbow upto tip of fingers . while starting the breathing move your left hand slowly upwards and behind your head till it touches the floor.<br>

• Synchronise your breathing with raising of your hand slowly till it rests behind your head . Hold for three counts with outstretched left hand behind your head.<br>

• Now slowly start breathing out the the same path ( reverse ) from finger tip down wards , elbow, shoulder joint , rib cage , stomach pelvic bones , thigh , knee , calf and out through the toe<br>

• while breathing out try to push the breath out from joints elbow, ankle, feet and out through toe .<br>

3) Repeat the conscious breathing with your right side also . Five Variation now with both legs and hands.<br>

4) Lie in Savasan position. Now start conscious breathing beginning with your toes of both legs simultaneousl y upwards toe , feet ,calf , pelvic joints but now the spinal column upwards towards back of your neck , small brain large brain (head ).<br>

<b>Note :</b><br>

While breathing you have to raise your both hands gradually till the hands rest behind your head out stretched. Hold for three counts in this position.<br>

5) Now start breathing out slowly from tip of fingers downwards , elbow , shoulder , ribcage, stomach , pelvic through ankle ,feet and toe .<br>

( Note the change –breath out through front and not spinal cord )<br>

<b>Note :</b><br>

Simultaneously while breathing out - slowly bring the hands down till each one rest on your sides .( back to savasan position )<br>

END of Exercise 1<br>
