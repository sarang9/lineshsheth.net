<b>Exercise 2</b><br>

<b>Start :</b><br>

1) Lie in savasan position .<br>

2) Slowly raise your left leg perpendicular to torso . In this position start conscious slow breathing beginning from toe downwards through feet ,ankle calf, knee , thigh until pelvic area / hip joint only.<br>

Immediately start breathing out in reverse direction pelvic , thigh , knee , calf and push out the breath through ankle ,feet .<br>

Repeat this cycle three times .<br>

3 ) Remain in starting position ie . Left leg perpendicular to Torso . Now stretch your right hand side ways perpendicular to torso and slowly bring your left leg side Ways with feet on the hand of your right palm .<br>

Now hold the left feet toe with Right hand fingers. Now converge your left hand on the toe of left leg feet. Your head sideways and eyes looking towards your hand holding the toe .<br>

Start conscious breathing from toe of left feet upwards ankle calf , knee,thigh and pelvic joint and upwards side ways to shoulder joint ,elbow and upto tip of fingers left hand out stretched on left side .<br>

<b>Note</b><br>

while breathing you have to gradually move your head towards left and your left hand also towards your left till your head is straight and eyes looking up and left hand by your left side out stretched . your Right hand fingers still holding the toe of left leg .<br>

