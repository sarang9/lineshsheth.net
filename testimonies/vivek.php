<p>09.12.2018</p>

<p>Hello Dear Friends of SOIL</p>

<p>I am Vivek Terkar, associated with SOIL from the days it was called "The Movement".</p>

<p>19th March 1990 was the day, I first met Linesh, when I was invited to attend a meeting at his residence in Latur, his home town.</p>

<p>The thoughts and ideas he shared were too good

and too great to be believed. It was just impossible to imagine that a person can be so selfless.. As days passed by, I started taking more interest in the activities and with that came more responsibilities. I could get a feeling of growth by engaging and helping lots of friends grow.</p>

<p>From a small beginning with few friends in the neighborhood, we turned to be the fastest growing group in the region with few hundred active, energetic friends. I can still recall the energetic members like Sunil Gilda, Sheetal, Rahul, Manoj, Prashant, Shrikant, Mangesh, Parimal, Balaji, etal who were just unstoppable</p>

<p>The meetings and retreats of Movement we had made us good managers. The opportunity to be a part of the delegates of Movement, visiting Italy and France in 1990 only broadened my vision.</p>

<p>I always feel that I have been very close to Linesh but could not till date become a good student.</p>

<p>Linesh introduced me to the teachings of Silo in the early days, took support of the teachings of Gurdijeff and Ouspensky to make us understand few things and recently also often referred Andrew Cohen in his teachings.</p>

<p>He has been evolving, He has been growing but the best part of our association has been his faith, hope and trust in individuals like me who are still far behind on the "Path" he walks and is always ready to hold my hand and make me walk with him.</p>

<p>This way of sharing has its own limitations and hence does not give justice to the quality, volume and intensity of my thoughts and feelings for Linesh and his valuable teachings.</p>

<p>But before I sign off, few one liners from Linesh that remain valid forever...</p>

<p>• There is no meaning in life, if

everything ends with death.

<p>• Life should have a scope beyond the necessities. Necessities are the mobilizer of energy, but not the giver of meaning</p>

<p>• Internal Unity, wherein our thoughts, feelings and actions do not oppose each other, is the producer of Peace, Force and Joy within us.

Many Thanks.. for innumerable things Dear Linesh

and other friends.</p>

