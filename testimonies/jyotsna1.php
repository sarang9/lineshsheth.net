<p>I met Mr. Linesh Sheth of SOIL (School of Internal Literacy) last month during a book reading session at SPJIMR, Mumbai and felt that a dialogue with him may give a direction to my 4-year-old cancer and certain psychological issues.</p>

<p>Today it was my third session with Mr. Linesh Sheth. We were discussing how one should strive for intentional growth instead of the mechanical growth, which makes us different as human beings. I learnt the science behind this kind of evolution. Its so simple yet so profound and effective, it is the 'Theory of Triad'.</p>

<p>When we mix two entirely different entities, the result we get is the third entity which is different from the previous two. So, we can mix smile with our breath to get entirely different state of mind. Visualization is a very powerful tool we humans possess. If we mix visualization with our breath, we can create wonders, like the one I experienced

today.</p>

