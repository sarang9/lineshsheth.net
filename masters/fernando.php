    <h2><center><b>How association with Fernando affected my life</b></center></h2>
    

<p><br>Fernando arrived in India around 1980. At that time i was just introduced to Silo's thoughts in Humanist Movement. I volunteered in its different activities because i liked the people, the spirit and selflessness of the whole approach. </p> 

<p> It was during 1981, when Silo addressed a public meeting comprising of 10000 people at Chowpatty beach in Mumbai, on 1st November 1981, that i had the opportunity to work closely with him in planning the event, spreading the Silo's message of Humanism and inspiring people to come to the event. </p>

<p>I had the privilege to work with Fernando along with many other committed volunteers. I could distinctly feel an inner obligation to follow what i was inspired with. The main reason was that i was in daily association with a man whom i liked. I respected the human values he stood for. Secretly i liked him as a model of life. Here was a man who was inspiring me to change my old pattern of behavior. That behavior was random, and directionless in its way of thinking. </p>

<p>Perhaps, in my heart, i was looking for a man who was not a Guru, whom traditionally i had to obey. I was also not looking for a teacher from whom i could learn, nor i was in search of a mythical model i would worship. Really, i did not know what kind of model i was seeking. </p>
<div class="col-lg-1 col-xs-2" style="margin:0px 10px; padding:0px;">
<img src="../images/gpstracksicon.png" class="img-responsive">
</div>
<p>Gradually it dawned upon me that i was looking for friendliness of a friend, a sort of a GPS (Global Positioning System) of life, a sort of a navigation system of living, in which i could locate where i was and how to go where i wanted to reach. Later i knew that in spiritual life, the GPS was represented by an "orientor". I saw that i was not alone in what i felt. There were hundreds who felt, more or less in different ways, the same way that i was feeling. </p>

<p>In this way i could see Fernando as a reference for life. Soon i found, following the  teachings he spread, that i was myself becoming, in a way, a reference in my family and in my surrounding in knowing how to live, direct and follow the path of light.</p>
