<p>One particular aspect that had a very deep influence on my comprehension of how to understand what happens inside me was Fernando's explanations, in different moments of my dialogues with him, about the internal states, as referred by Silo in chapter Nineteen(XIX)of the book, Inner Look. In those days, Fernando explained the chapter in its original version to me, as analogous to Kabbalah's Tree Of Life. The vivid description of the passages he explained influenced me so much that i later became, more or less, a student of Kabbalah by studying various books and explanations on Kabbalah in great detail. Even today, Kabbalah personally acts with me like my inner GPS(Global Positioning System). I have been able to change, shift and transform my inner states very often by following what was explained then. </p>

