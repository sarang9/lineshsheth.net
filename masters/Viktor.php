                    <h3 align="center"><b>About Viktor frankl</b></h3>
                    <div align="center"><img style="height:300px;" class="img-responsive" src="../images/viktor-frankl.jpg"></div>
                    
<p>Early in my career, life was busy with struggle to earn to have a comfortable survival. I had lack of means in life in terms of a place to live and money to survive. It was during one of my periods of daily mediation that i felt, a comfortable survival is important but  there has to be something more than survival for a man, as otherwise, he makes no difference between him and an animal. <b>I did not know what that "something more" was,</b> until i heard, during one of the lectures given by <b>Silo, where he said: “Life has a Meaning".</b></p>
<div class="col-lg-2" >
  <img src="../images/IMG-20170116-WA0018.jpg" class="img-responsive"><br>
  <img src="../images/vfbook.jpg" class="img-responsive">
</div>
<p><br>In his book, Inner Look, Silo said: “If you live or die or were not born at all makes no difference to the world, your life has no meaning". He further said that "Life has no meaning if everything ends with death". There were several discussions, time and again, with Fernando, my guide and mentor, and my humanist colleagues on this subject. That environment made me curious to know more about meaning of life.<br></p>

<div class="col-lg-2 pull-right" >
  <img src="../images/vfbook1.jpg" class="img-responsive">
</div>

<p><br>It was during this period of my psychological obsession about meaning of life that i came across the writings of Viktor Frankl, one of most influential and enduring writers and psycho-therapists of 21th century. Frankl was also called the prophet of meaning of life and was known during his life as a Voice of hope and contribution to positive psychology. I read his famous book "Man's Search for Meaning", "Man's Search for Ultimate Meaning" and several other books written by those who worked lifelong on his proposal of Logo therapy.<br></p>

<p><br>Frankl's writings, his illustrations and affirmations, were so convincing that i decided to become a diplomat on meaning of life (Logo therapy) through his one year distance learning course from USA. The more i read him, the more i remained conscious on making my own life meaningful. What i liked about Frankl what his saying that:</p>

<p> 1. "What man actually needs is not a tensionless state but rather the striving and struggling for some goal worthy of him. What he needs is not the discharge of tension at any cost but the call of potential meaning waiting to be fulfilled by him". The contemplation on this aspect changed my seeking in life. Earlier, i was seeking peace in life but this statement made me revise my goal from seeking of peace to seeking for a purpose and meaning of life.</p>
<div class="col-lg-1 pull-right" >
  <img src="../images/Esoteric_Cosmology1.png" class="img-responsive">
</div>
<div class="col-lg-1 pull-right" >
  <img src="../images/51.png" class="img-responsive">
</div>
<p> 2. Frankl spoke of <b>"intensification of inner life"</b> that helped prisoners stay alive. he considered the trascendental power of love: <b>"Love goes very far beyond the physical person of the beloved. It finds its deepest meaning in his spiritual being, his inner self. Whether or not he is actually present, whether or not he is still alive at all, ceases somehow to be of importance."</b></p>
<div class="col-lg-3" align="center">
  <img src="../images/53.jpg" class="img-responsive">
</div>
<p> 3. On actions in daily life Frankl said "Life ultimately means taking the responsibility to find the right answers to its problems and to fulfill the tasks which it constantly sets for each individual" he said, <b>"It is not what happens to us that matters.  It is how we respond to what happens that is significant.  Same with business. We cannot control all the elements of our personal life or business."</b></p>

<p> 4. Frankl said freedom in life is not the last word; it is only part of story and half of the truth. Freedom is but the negative aspect whole phenomenon whose positive aspect is irresponsibleness. <b> Without responsibility in life, freedom degenerates into mere arbitrariness. What is really needed is a fundamental change in our attitude towards life. We have to learn that it did not really matter what we expected from life, but rather what life expected from us. Life constantly questions us in each situation, what the right action is and what right conduct that gives meaning to it is.</b></p>

<b>
<p> 5. Frankl's Logotherapy is trifocal. It focuses on three fundamental facts of human existance: A will to meaning, a meaning in suffering, and a freedom of will. </p>

<p><i> About meaning in suffering, Frankl states that human potential, at its best, is to turn tragedy into triumph. He says, the measure of a man is the way he bears up under misfortune. Regarding man's freedom of choice, Frankl's says, the freedom is not only to choose one's own way of living, but even one's way of dying.</i> </p>
</b>

<p>I would say along with Silo, the one who influence my life deeply is undoubtedly the work of Viktor Frankl in the field of positive psychology that aims at giving meaning to life.</p>






