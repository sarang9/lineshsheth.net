<p>The first time I read the line <b><i>“Live as if you were living already for the second time and as if you had acted the first time as wrongly as you are about to act now!”</i></b> I was in a very trying time in my life. Those words sparked a fire in me that got me through those dark times and I was forever grateful. But, at the time, I had no idea where those words came from. So years later, I looked it up. That is when I discovered Man’s Search for Meaning, and the amazing story of Viktor E. Frankl.</p>
<p>Frankl was a successful Jewish psychologist in Vienna, who in 1942 was arrested and placed into a concentration camp along with his family. 3 years later when that concentration camp was liberated, most of Viktor’s family was dead. Including his pregnant wife.</p>
<p>In 1946 Viktor wrote Man’s Search for Meaning, which detailed his time in the camp where he worked as a therapist. Imagine that scenario: a Jewish therapist in a concentration camp. When I first started reading the book, I couldn’t think of a more soul-crushing experience…and then I got to this:</p>

<ul style="list-style-type: square;">
<li><p><b> “A thought transfixed me: for the first time in my life I saw the truth as it is set into song by so many poets, proclaimed as the final wisdom by so many thinkers. The truth — that love is the ultimate and the highest goal to which man can aspire. Then I grasped the meaning of the greatest secret that human poetry and human thought and belief have to impart: The salvation of man is through love and in love. I understood how a man who has nothing left in this world still may know bliss, be it only for a brief moment, in the contemplation of his beloved. In a position of utter desolation, when man cannot express himself in positive action, when his only achievement may consist in enduring his sufferings in the right way — an honorable way — in such a position man can, through loving contemplation of the image he carries of his beloved, achieve fulfillment. For the first time in my life I was able to understand the meaning of the words, ‘The angels are lost in perpetual contemplation of an infinite glory.'”</b></p></li>
</ul>

<p>Those words changed my life. In honor of Viktor E. Frankl, I have chosen some of my favorite passages in the book, and I hope they touch your soul the way that they have touched mine.</p>

<b>
<ul style="list-style-type: square;">

<li><p> “Everything can be taken from a man but one thing; the last of the human freedoms — to choose one’s attitude in any given set of circumstances, to choose one’s own way.”</p></li>
<li><p> “Don’t aim at success — the more you aim at it and make it a target, the more you are going to miss it. For success, like happiness, cannot be pursued; it must ensue, and it only does so as the unintended side-effect of one’s personal dedication to a cause greater than oneself or as the by-product of one’s surrender to a person other than oneself. Happiness must happen, and the same holds for success: you have to let it happen by not caring about it. I want you to listen to what your conscience commands you to do and go on to carry it out to the best of your knowledge. Then you will live to see that in the long run — in the long run, I say — success will follow you precisely because you had forgotten to think of it.”</p></li>
<li><p> “In some way, suffering ceases to be suffering at the moment it finds a meaning, such as the meaning of a sacrifice.”</p></li>
<li><p> “When we are no longer able to change a situation, we are challenged to change ourselves.”</p></li>
<li><p> “Between stimulus and response, there is a space. In that space is our power to choose our response. In our response lies our growth and our freedom.”</p></li>
<li><p> “It is well known that humor, more than anything else in the human make-up, can afford an aloofness and an ability to rise above any situation, even if only for a few seconds.”</p></li>
<li><p> “Man’s main concern is not to gain pleasure or to avoid pain but rather to see a meaning in his life.”</p></li>
<li><p> “The more one forgets himself — by giving himself to a cause to serve or another person to love — the more human he is and the more he actualizes himself. What is called self-actualization is not an attainable aim at all, for the simple reason that the more one would strive for it, the more he would miss it. In other words, self-actualization is possible only as a side-effect of self-transcendence.”</p></li>
<li><p> “Man’s search for meaning may arouse inner tension rather than inner equilibrium. However, precisely such tension is an indispensable prerequisite of mental health. There is nothing in the world, I venture to say, that so effectively helps one to survive even the worse conditions as the knowledge that there is a meaning in one’s life.”</p></li>
<li><p> “Man does not simply exist but always decides what his existence will be, what he will become in the next moment.”</p></li>
<li><p> “One should not search for an abstract meaning of life. Everyone has his own specific vocation or mission in life to carry out a concrete assignment which demands fulfillment. Therein he cannot be replaced, nor can his life be repeated. Thus, everyone’s task is as unique as is his specific opportunity to implement it.”</p></li>
<li><p> “Love is the only way to grasp another human being in the innermost core of his personality. No one can become fully aware of the very essence of another human being unless he loves him.”</p></li>

</b>
</ul>
