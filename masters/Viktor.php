    <h2><center>How Viktor Frankl's teachings influenced my life</center></h2>
    

<p>My obsession of discovering meaning of life was ignited basically by writings, sayings and teachings of Silo. The idea of meaning of life as a clear desire of life percolated slowly as i got involved into work of humanist movement deeply. During that period meaning of life appeared to be something inherent to life but had a mystical touch for me. I could not clearly define it nor experienced it as a felt sensation of what can be called a meaning of life. However, later during my involvement, Silo introduced four Disciplines of School wherein Purpose of life was stressed upon as a necessary conducting thread of life. Yet both purpose and meaning had a little transcendent and mystical connotation. I could not come to grasp the idea that i can touch, feel and realize. This was my inability.</p>
<div class="col-lg-2" align="center">
  <img src="../images/life-purpose.jpg" class="img-responsive">
</div>
<p>However when i came in touch with Viktor Frankl's writings, he clarified that meaning was something to be discovered in everything one does and experiences in life including one's suffering. His book "Man's Search for Meaning" and "Man's Search for Ultimate Meaning of Life" provided a concrete understanding of how meaning can be discovered. He even had formulated a PIL (Purpose in Life) test procedure that help one discover it. </p>
<div class="col-lg-4 pull-right" align="center">
  <img src="../images/50.jpg" class="img-responsive">
</div>
<p>Following this, i chose to complete a distance learning course of Logo therapy which concerns with solution to the life through discovery of meaning. The course gave a Humanist approach to meaning that differs distinctly from therapy and therapists like the Sigmund Freud and Alfred Adler. In process, it made me constantly aware of what is meaningful, what is purposeful and what is not, in life. </p>
<div class="col-lg-2" align="center">
  <img src="../images/52.jpg" class="img-responsive">
</div>
<p>Of particular importance was Frankl's reference to the fact that "There is more to life than being just happy". A life can be apparently unhappy, yet meaningful. What is of importance is not pursuit of happiness in terms of having a roof over head, food to eat, monetary comfort and life of negative freedom to do what one likes, whether it is moral or immoral, but a life that gives meaning and substance to it. In a fact a consumerist life for pursuit of happiness is, in effect, is a life of being a "taker" whereas a meaningful life is a life of being a "giver".</p>
<div class="col-lg-2 pull-right" align="center">
  <img src="../images/51.jpg" class="img-responsive">
</div>
<p>It set me thinking when i saw that there were abundant examples in history and in everyday life wherein people willfully chose a difficult apparently torturous and suffering life in order to give meaning to it. Gandhi chose an ascetic and simple life, more prone to inconvenience, rather than a comfortable life because he was wedded to larger and meaningful cause of freedom of India. Thousands of ascetics, revolutionaries, freedom fighters and cause oriented people do the same because meaning they achieve in life is far greater than a simple and comfortable happy life.</p>

<div class="col-lg-2" align="center">
  <img src="../images/54.jpg" class="img-responsive">
</div>
<p>Viktor Frankl's entry into my life, i can say, gave more definition and clarity about meaning and purpose. Purpose, according to Frankl, is translated in terms of having meaningful goals. Goals essentially become meaningful when they have to do with helping others and helping them to grow in life.</p>

<p>Viktor Frankl's teachings helped me clarify discovery of meaning and purpose. It threw clear light on them.</p>

