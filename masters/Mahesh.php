    <h2 style="font-family: 'Oswald', sans-serif;"><center>About the Practice of TM as i was taught and i practiced<br></center></h2>

<p>The technique is recommended for 20 minutes twice per day. According to the Maharishi, "bubbles of thought are produced in a stream one after the other", and the Transcendental Meditation technique consists of experiencing a "proper thought" in its more subtle states "until its subtlest state is experienced and transcended". Because it is mantra based, the technique "ostensibly meets the working definition of a concentration practice"; however, the TM organization says that "focused attention" is not prescribed, and that the "aim is an [sic] unified and open attentional stance". Other authors describe the technique as an easy, natural technique or process, a "wakeful hypometabolic physiologic state". Practice of the technique includes a process called "unstressing" which combines "effortless relaxation with spontaneous imagery and emotion". TM teachers caution their students not to be alarmed by random thoughts and to "attend" to the mantra. Scottish chess grandmaster Jonathan Rowson has said that his TM practice gives "a feeling of serenity, energy and balance", but does not provide "any powerful insight into your own mind". Laura Tenant, a reporter for The Independent, said that her TM experience includes going "to a place which was neither wakefulness, sleeping or dreaming", and becoming "detached from my physical self". Worldwide, as many as four to ten million people are reported to be practitioners.</p>

<p style="color:#fff; background-color:#006966; padding:10px; font-size:19px;"><b>Mantra</b></p>


<p>The TM technique consists of silently repeating a mantra with "gentle effortlessness" while sitting comfortably with eyes closed and without assuming any special yoga position. The mantra is said to be a vehicle that allows the individual's attention to travel naturally to a less active, quieter style of mental functioning. One author discusses neurological theories about the importance of selecting the correct mantra. According to these, the mantra enters "the central nervous system via the brain's speech area", and represents "a direct input of ease and order". TM meditators are instructed to keep their mantra secret to ensure maximum results ("speaking it aloud, apparently defeats the purpose"), to avoid confusion in the mind of the meditators, and as a "protection against inaccurate teaching".</p>

<p style="color:#fff; background-color:#006966; padding:10px; font-size:19px;"><b>Selection</b></p>

<p>The Maharishi is reported to have standardized and "mechanized" the mantra selection process by using a specific set of mantras and making the selection process "foolproof". Professor of psychiatry, Norman E. Rosenthal writes that during the training given by a certified TM teacher, "each student is assigned a specific mantra or sound, with instructions on its proper use". The Maharishi said that the selection of a proper thought or mantra "becomes increasingly important when we consider that the power of thought increases when the thought is appreciated in its infant stages of development". He said that mantras chosen for initiates should "resonate to the pulse of his thought and as it resonates, create an increasingly soothing influence", and that the chosen mantra's vibrations "harmonize" with the meditator, and suits their "nature and way of life". TM students are therefore given a "specially suited mantra". Author George D. Chryssides writes that, according to the Maharishi, "using just any mantra can be dangerous", the mantras for "householders" and for recluses differ. The Transcendental Meditation mantras are appropriate mantras for householders, while most mantras commonly found in books, such as "Om", are mantras for recluses and "can cause a person to withdraw from life".</p>

<p>Former TM teacher and author Lola Williamson reports that she told her TM students that their mantra was chosen for them based on their personal interview, while sociologist Roy Wallis, religious scholar J. Gordon Melton and Bainbridge write that the mantras are assigned by age and gender. In 1984, 16 mantras were published in Omni magazine based on information from "disaffected TM teachers". According to Chryssides, TM teachers say that the promised results are dependent on a trained Transcendental Meditation teacher choosing the mantra for their student.</p>

<p style="color:#fff; background-color:#006966; padding:10px; font-size:19px;"><b>Meaning and sound value</b></p>

<p>In his 1963 book The Science of Being and Art of Living, the Maharishi writes that words create waves of vibrations, and the quality of vibration of a mantra should correspond to the vibrational quality of the individual. Likewise, religious studies scholar Thomas Forsthoefel writes, "the theory of mantras is the theory of sound". Author William Jefferson writes that the "euphonics" of mantras are important. Sociologist Stephen J. Hunt and others say that the mantra used in the Transcendental Meditation technique "has no meaning", but that "the sound itself" is sacred. In Kerala, India, in 1955, the Maharishi spoke of mantras in terms of personal deities, and according to religious studies scholar Cynthia Ann Humes, similar references can be found in his later works.</p>

<p>According to authors Peter Russell and Norman Rosenthal, the sounds used in the technique are taken from the ancient Vedic tradition, have "no specific meaning". They are selected for their suitability for the individual. Author, Lola Williamson writes that the bija, or seed mantras used in TM come from the Tantric, rather than Vedic tradition, and that bija mantras are "traditionally associated with particular deities and used as a form of worship". According to Needleman, many mantras come from the Vedas or Vedic hymns, which are "the root for all later Hindu scripture", while the 1977 court case Malnak vs. Yogi accepted the TM mantras as meaningless sounds. Likewise, philosophy of science scholar and former Maharishi International University professor Jonathan Shear writes in his book The Experience of Meditation: Experts Introduce the Major Traditions, that the mantras used in the TM technique are independent of meaning associated with any language, and are used for their mental sound value alone. Fred Travis of the Maharishi University of Management writes in a 2009 article published in the International Journal of Psychophysiology that "unlike most mantra meditations, any possible meaning of the mantra is not part of Transcendental Meditation practice".</p>
    

