<p><b>Interbeing nature of truths and times:</b> The past, present, and the future inter-are. By getting in touch with the present moment, we can get in touch with the past and the future. Relative truths and absolute truths inter-are. By looking deeply into relative truth, we can realize absolute truth. Peace and happiness is found in pain and suffering. They inter are. The pure land exists in the here and now, not in a distant future and in a faraway place. This insight helps us to cultivate Right View, by removing the duality view which sees things as opposite pairs such as coming/going, birth/death, success/failure, being/non-being.</p>
