                <p><center><b>Practice of mindfulness on 16 breaths <br>as taught by Thich Nath Hanh</b></center></p>

<p><b><br>I have gone ahead and decided to list out the 16 things that we are supposed to observe when practicing Mindfulness of Breath, this translation was taken from Vietnamese Zen Master Thich Nhat Hanh's translation of the Anapanasati Sutta, otherwise translated as the Mindfulness of Breath Sutta</b></p>

<div class="col-lg-3 pull-right" align="center">
<img src="../images/thich nath hanh.jpg" class="img-responsive">
</div>
<b>
<p>1. “Breathing in, I am aware I am breathing in.” ”Breathing out, I am aware that I am breathing out.”</p>
<p>2.“Breathing in a long breath, I know I am breathing in a long breathe. Breathing out, I know I am breathing out a long breath.“</p>
<p>3. “Breathing in, I am aware of my body. Breathing out, I smile to my body.”</p>
<p>4. “Breathing in, I am aware of my body. Breathing out, I calm the activities of my whole body and release any tension in my body.”</p>
<p>5. "Breathing in I am aware of the energy of joy. Breathing out, I smile to the energy of joy.”</p>
<p>6. “Breathing in, I am aware of the energy of happiness in me. Breathing out, I smile to the energy of happiness.”</p>
<p>7. "Breathing in, I am aware of a feeling present in me (pleasant, unpleasant & neutral). Breathing out, I smile to the feeling present in me.”</p>
<p>8. “Breathing in, I calm my feelings. Breathing out, I calm my feelings.”</p>
<p>9. “Breathing in, I am aware of my mind. Breathing out, I am aware of my mind.”</p>
<p>10. “Breathing in, I make my mind happy. Breathing out, I make my mind happy.”</p>
<p>11. “Breathing in, I skillfully concentrate my mind. Breathing out, I skillfully concentrate my mind.</p>
<p>12. “Breathing in, I skillfully liberate my mind. Breathing out, I skillfully liberate my mind.”</p>
<p>13. “Breathing in, I observe objects of my mind. Breathing out, I observe the objects of my mind“</p>
<p>14. “Breathing in, I observe my attachments and unwholesome desires. Breathing out, I observe the dangers of my attachments and unwholesome desires.”</p>
<p>15. “Breathing in, I observe the no-birth, no-death nature of objects of mind. Breathing out, I observe the no-birth, no-death nature of objects of mind.”</p>
<p>16. “Breathing in, I observe letting go. Breathing out, I observe letting go.”</p>
<p>Conveniently, the first four of sixteen exercises are focused on developing awareness of the body (and breath), the second on awareness of our feelings, the third on awareness of our mind, and last on awareness of our mind-objects. </p>

