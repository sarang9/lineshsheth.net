<p><b>Go as a river:</b> Sangha is one of the three jewels of Buddhism. “I take refuge in the Sangha, the community that lives in harmony and awareness.” Practicing with the Sangha benefits us in many ways. With the support of the Sangha, we can correct bad habit energies, which would be difficult to fix if we practice alone. We consider the Sangha as our family, and accept other members as family members. Sangha can help us heal. Zen Master Thich Nhat Hanh thinks that the next Buddha may manifest in the form as a community, not as an individual. We need the whole community to wake up together.<br><br></p>
