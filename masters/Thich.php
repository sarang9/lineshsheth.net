    <h2><center>How Thich Nath Hanh's teachings influenced my life</center></h2>

<p>During my search to have an insight into various methods of meditations in the world, i wanted to search into different ways in which <b>Vipassana</b>, the Buddist meditation, was practiced. I found that Thich Nhat Hanh had a particular method that concentrated on <b>16 breaths in which one gets an insight into himself thorugh mindfulness of breathing listed here seperately</b>.</p>
<p>I followed his methods, as described in his book <b><i>"Breathe, You Are Alive"</i></b> for a long time and even taught to many people. I was attracted by his concept of being engaged with the world to play an active role in creating change. His writings are very simple, persuasive and non-violent in their core. I read several of his books repeatedly and they changed my way of being with myself, with others and with the situations of daily life. He has left very positive impact on my life. </p>
<p>I was deeply impressed and involved with his approach of loving oneself. Thich says that loving oneself involves mindfulness that embracess the body, the feelings, the mind and the objects of the mind as if they are your own children. </p>
<p>So in a way he proposes Love Meditation as a method developing mind of love and compassion. Love is a mind which is intent on bringing peace, joy, happiness to oneself and to others. Compassion is a mind which is intent on removing the suffering which is present in you and others. In a way, he says, "Love is the capacity to give joy". Compassion is the power to relieve suffering. He proposes that we should daily water the seeds of love and compassion within our mind field by mindful breathing.</p>
<p>Typically in Thich's method, one does not try to dislike oneself when he notices within anger, jealousy, trauma etc. but learns how to talk and pursue his mind that it harms itself with these contents. Therefore he transforms them into positive energy of smile to oneself with mindful breathing. I would recommend at least two of his books that i have read many times. One is, <b><i>"Breath You Are Alive"(Amazon:- kindle edition Rs.140, Paperback Rs.394)</b></i> and other is <b><i>"Transformation & Healing"(Amazon:- kindle edition Rs.439, Paperback Rs.801)</b></i> as on 2017.</p>
<p>Other dimensions of Thich's teachings that deeply influenced me were:</p>

          <p>1. Teachings and practices of Plum Village</p>
          <p>2. The Five Mindfulness Trainings</p>
          <p>3. Beginning Anew</p>
          <p>4. The Peace Treaty</p>

<p>In fact, these teachings were presented to me as a newer version of Silo's teachings that i was involved with already. I had practiced and taught them in another form. But Thich's way of presenting them facilated my understanding. I feel immensly grateful to Thich for adding a new perspective to the teaching that i was already involved with.</p>


