<p><center style="font-size:28px;"><b>Beginning Anew</b></center></p>

<p>To begin anew is to look deeply and honestly at ourselves, our past actions, speech and thoughts and to create a fresh beginning within ourselves and in our relationships with others. At the practice center we practice Beginning Anew as a community every two weeks and individually as often as we like.</p>

<div class="col-lg-2 pull-right" align="center">
<img src="../images/20.jpg" class="img-responsive">
</div>

<p>We practice Beginning Anew to clear our mind and keep our practice fresh. When a difficulty arises in our relationships with fellow practitioners and one of us feels resentment or hurt, we know it is time to Begin Anew. The following is a description of the four-part process of Beginning Anew as used in a formal setting. One person speaks at a time and is not interrupted during his or her turn. The other practitioners practice deep listening and following their breath.</p>

<p><b>1) Flower watering</b></p>

<div class="col-lg-2" align="center">
<img src="../images/21.jpg" class="img-responsive">
</div>

<p>This is a chance to share our appreciation for the other person. We may mention specific instances when the other person said or did something that we had admired. This is an opportunity to shine light on the other’s strengths and contributions to the Sangha and to encourage the growth of his or her positive qualities.</p>

<div class="col-lg-2 pull-right" align="center">
<img src="../images/22.jpg" class="img-responsive">
</div>
<p><b>2) Sharing regrets</b></p>

<p>We may mention any unskillfulness in our actions, speech or thoughts that we have not yet had an opportunity to apologize for.</p>

<p><b>3) Expressing a hurt</b></p>
<div class="col-lg-2" align="center">
<img src="../images/23.png" class="img-responsive">
</div>

<p>We may share how we felt hurt by an interaction with another practitioner, due to his or her actions, speech or thoughts. (To express a hurt we should first water the other person’s flower by sharing two positive qualities that we have truly observed in him or her. Expressing a hurt is often performed one on one with another practitioner rather than in the group setting. You may ask for a third party that you both trust and respect to be present, if desired.)</p>

<p><b>4) Sharing a long term difficulty & asking for support</b></p>


<div class="col-lg-2 pull-right" align="center">
<img src="../images/24.jpg" class="img-responsive">
</div>
<p>At times we each have difficulties and pains arise from our past that surface in the present. When we share an issue that we are dealing with we can let the people around us understand us better and offer the support that we really need.</p>
<div class="col-lg-2" align="center">
<img src="../images/23.jpg" class="img-responsive">
</div>
<p>The practice of Beginning Anew helps us develop our kind speech and compassionate listening. Beginning Anew is a practice of recognition and appreciation of the positive elements within our Sangha. For instance, we may notice that our roommate is generous in sharing her insights, and another friend is caring towards plants. Recognizing others positive traits allows us to see our own good qualities as well.</p>

<p>Along with these good traits, we each have areas of weakness, such as talking out of our anger or being caught in our misperceptions. When we practice “flower watering” we support the development of good qualities in each other and at the same time we help to ease the difficulties in the other person. As in a garden, when we “water the flowers” of loving kindness and compassion in each other, we also take energy away from the weeds of anger, jealousy and misperception.</p>

<p>We can practice Beginning Anew every day by expressing our appreciation for our fellow practitioners and apologizing right away when we do or say something that hurts them. We can politely let others know when we have been hurt as well. The health and happiness of the whole community depends on the harmony, peace and joy that exist between every member in the Sangha.</p>

