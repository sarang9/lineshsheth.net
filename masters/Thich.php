<p><b>I have arrived, I am home:</b> This is the practice of “Dwelling Happily in the Present Moment”. We need to slow down, to stop running after things. We need to go back to ourselves, to calm our body and mind, to look deeply into true elements of our happiness. We often look for happiness in the future. With mindfulness, we can realize that happiness is real and available the present moment. Mindfulness is the energy that helps us to be aware of what’s happening in our body and mind, in the environment around us, to be in touch with the present moment. If we are mindful, we can realize wonders of life manifested around us. We can touch the pure land in the here and now. Living mindfully in the present moment, we can take good care of the past and the future. Present moment is the true home for our body and mind. </p>
