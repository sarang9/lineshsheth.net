<p><b>Our consciousness is continuously ripening:</b></p>

<p>The teachings of the Manifestation School say that all life phenomena –whether they are physical or psychological, are manifested by our consciousness. Phenomena have their starts in the form of seeds. These seeds represent all actions, experiences and perceptions that we have done in our lives. There are four kinds of consciousnesses. The first one is the five sense consciousnesses, which arise when our eyes see forms, our ears hear sounds, our nose smells odors, our tongue tastes something and our body touches an object. The second one is mind consciousness, which arises when our mind get in contact with an object of perceptions. The third one is manas consciousness, which gives rises and supports to the mind consciousness. Manas consciousness arises from store consciousness, takes a part of store consciousness as its object of love. It considers this part as its identity and its separate self, and holds on to it firmly. Manas consciousness is the base of the “I, me, mine” perception. Therefore, it has a discriminative nature. The fourth one is store consciousness, from which all other consciousness arise. Store consciousness stores, preserves, and processes all the seeds. The seeds transmitted to us generations after generations.</p>

<p>It is the characteristics of these seeds, and how we take care of these seeds that determine the quality of our life. There are wholesome seeds, unwholesome seeds and neutral seeds. Our thought actions, speech actions and bodily actions can create conditions for these seeds to manifest, to mature and to transform. When these seeds are transformed, we are also transformed. We are the sum of our actions. Our peace and happiness depends on how we live.</p>

<p>The Four-Fold Right Diligence practice (of the Noble Eightfold path) tells us to create conditions for (a) unwholesome seeds to stay in the store consciousness (b) unwholesome seeds to return to the store consciousness if they arise in the mind consciousness (c) wholesome seeds to arise in the mind consciousness and (d) wholesome seeds to stay and to grow stronger if they arise in the mind consciousness. The seeds manifested in the mind-consciousness are called mental formations. Manas has the tendency for unwholesome mental formations to arise in the mind consciousness. When those things happen, mind consciousness can use the energy of mindfulness to embrace and shine light on the unwholesome mental formations, seeing their impermanent and interbeing nature, and helps manas to transform its discriminative to a non-discriminative nature. The unwholesome mental formations in the mind consciousness then also are transformed.</p>

<p>So, it is matter of nature (seeds) and nurture (conditions). The important points to note are the seeds change continuously and mindfulness help make the changes. Plum Village practices employ these principles to plan its mindfulness activities.</p>

