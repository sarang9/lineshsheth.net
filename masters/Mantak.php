<p>2.  Inhale one more deep breath and lightly contract the arm muscles to squeeze the Chi into the bones. Hold for a while to condense the Chi into the marrow of your bones. Exhale and at the same time feel a distinct heaviness in the bones, meaning that the Chi has been condensed and packed into the bones successfully. Eventu-ally you will use more mental power (mind/eyes/heart power) and less muscle, us-ing soft breathing, to draw the force into the bones. Do 6 to 9 times; rest and feel the Chi that has been condensed into the bones.</p>
