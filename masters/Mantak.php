<p>There are several variations in the Bone Breathing and Marrow Washing process. Here we will introduce you to the first type of Bone Breathing, inhaling and exhaling Chi through the skin and packing it into the bones. In this method, you imagine that your bones are like hollow tubes, and that you breathe and suck the Chi into the bones.</p>
<p>Bone Breathing is practiced in a three-stage process. Let your breathing follow a normal pace. Do not strain or hold your breath too long.</p>

